      <p class="f-cont">
        呼吸运动是一种节律性的活动，其深度和频率随体内、外环境条件的改变而改变例如劳动或运动时，代谢增强，呼吸加深加快，肺通气量增大，摄取更多的O2，排出更多的CO2，以与代谢水平相适应。呼吸为什么能有节律地进行？呼吸的浓度和频率又如何能随内、外环境条件而改变？这些总是是本节的中心。
        <br> 一、呼吸中枢与呼吸节律的形成
        <br> 呼吸中枢是指中枢神经系统内产生和调节呼吸运动的神经细胞群。多年来，对于这些细胞群在中枢神经系统内的分布和呼吸节律产生和调节中的作用，曾用多种技术方法进行研究。如早期的较为粗糙的切除、横断、破坏、电刺激等方法，和后来发展起来的较为精细的微小电毁损、微小电刺激、可逆性冷冻或化学阻滞、选择性化学刺激或毁损、细胞外和细胞内微电极记录、逆行刺激（电刺激轴突，激起冲动逆行传导至胞体，在胞体记录）、神经元间电活动的相关分析以及组织化学等方法。有管些方法对动物呼吸中枢做了大量的实验性研究，获得了许多宝贵的资料，形成了一些假说或看法。
        <br> （一）呼吸中枢
        <br> 呼吸中枢分布在大脑皮层、间脑、脑桥、延髓和脊髓等部位。脑的各级部位在呼吸节律产生和调节中所起作用不同。正常呼吸运动是在各级呼吸中枢的相互配合下进行的。
        <br> 1．脊髓&nbsp;&nbsp;脊髓中支配呼吸肌的运动神经元位于第3-5颈段（支配膈肌）和胸段（支配肌间肌和腹肌等）前角。很早就知道在延髓和脊髓间横断脊髓，呼吸就停止。所以，可以认为节律性呼吸运动不是在脊髓产生的。脊髓只是联系上（高）位脑和呼吸肌的中继站和整合某些呼吸反射的初级中枢。
        <br> 2．下（低）位脑干&nbsp;&nbsp;下（低）位脑干指脑桥和延髓。横切脑干的实验表明，呼吸节律产生于下位脑干，呼吸运动的变化因脑干横断的平面高低而异。
        <br> 在动物中脑和脑桥之间进行横切，呼吸无明显变化。在延髓和脊髓之间横切（D平面），呼吸停止。上述结果表明呼吸节律产生于下位禽干，上位脑对节律性呼吸不是必需的。如果在脑桥上、中部之间横切（B平面），呼吸将变慢变深，如再切断双侧迷走神经，吸气便大大延长，仅偶尔为短暂的呼气所中断，这种形式的呼吸称为长吸呼吸。这一结果是提示脑桥上部有抑制吸气的中枢结构，称为呼吸整中枢；来自肺部的迷走传入冲动也有抑制吸气的作用，当延髓失去来自这两方面对吸气活动的抑制作用后，吸气活动不能及时中断，便出现长吸呼吸。再在脑桥和延髓之间横切（C平面），不论迷走神经是否完整，长吸式呼吸都消失，而呈喘息样呼吸，呼吸不规则，或平静呼吸，或两者交替出现。因而认为脑桥中下中有活化吸气的长吸中枢；单独的延髓即可产生节律呼吸。孤立延髓的实验进一步证明延髓可独立地产生节律呼吸。于是在20-50年代期间形成了三级呼吸中枢理论；脑桥上部有呼吸调整中枢，中下部有长吸中枢，延髓有呼吸节律基本中枢。后来的研究肯定了早期关于延髓有呼吸节律基本中枢和脑桥上部有呼吸调整中枢的结论，但未能证实脑桥中下部存在着结构上特定的长吸中枢。
        <br> 用微电极等新技术研究发现，在中枢神经系统内有的神经元呈节律性放电，并和呼吸周期相关，这些神经元被称为呼吸相关神经元或呼吸神经元。这些呼吸神经元有不同类型。就其自发放电的时间而言，在吸气相放电的为吸气神经元，在呼气相放电的为呼气神经元，在吸气相放电并延续至呼气相的为吸气-呼气神经元，在呼气相放电并延续到吸气相者，为呼气-吸气神经元，后两类神经元均系跨时相神经元。
        <br> 在延髓，呼吸神经元内主要集中在背侧（孤束核的腹外侧部）和腹侧（疑核、后疑核和面神经后核附近的包氏复合体）两组神经核团内，分别称为背侧呼吸组（dorsal respiratory groupDRG）和腹侧呼吸组（ventralrespiratory groupVRG）。背侧呼吸组的神经元轴突主要交叉到对侧，下行至脊髓颈段，支配膈运动神经元。疑核呼吸神经元的轴突由同侧舌咽神经和迷走神经传出，支配咽喉部呼吸辅助肌。后疑核的呼吸神经元绝大部分交叉到对侧下行，支配脊髓肌间内、外肌和腹肌的运动神经元，商品化纤维也发出侧支支配膈肌的运动神经元。包氏复合体主要含呼气神经元，它们的轴突主要与背侧呼吸组的吸气神经元形成抑制性联系，此外也有轴突支配脊髓的膈运动神经元。
        <br> 由于延髓呼吸神经元主要集中在背侧呼吸组和腹侧呼吸组，所以曾推测背侧呼吸组和腹侧呼吸组是产生基本呼吸节律的部位。可是，后来的某些实验结果不支持这一看法。有人用化学的或电解的毁损这些区域后，呼吸节律没有明显变化，这些结果提示背侧呼吸组和腹侧呼吸组可能不是呼吸节律唯一发源地，呼吸节律可能源于多个部位，产生呼吸节律的神经结构相当广泛，所以不容易因局灶损害而丧失呼吸节律。
        <br> 在脑桥上 部，呼吸神经元相对集中于臂旁内侧核和相邻的Kolliker-Fuse（KF）核，合称PBKF核群。PBKF和延髓的呼吸神经核团之间有双向联系，形成调控呼吸的神经元回路。在麻醉猫，切断双侧迷走神经，损毁PBKF可出现长吸，提示早先研究即已发现的呼吸调整中枢乃位于脑桥的BPKF，其作用为限制吸气，促使吸气向呼气转换。
        <br> 3．上位脑&nbsp;&nbsp;呼吸还受脑桥以上部位的影响，如大脑皮层、边缘系统、下丘脑等。
        <br> 大脑皮层可以随意控制呼吸，发动说、唱等动作，在一定限度内可以随意屏气或加强加快呼吸。大脑皮层对呼吸的调节系统是随意呼吸调节系统，下位脑干的呼吸调节系统是自主节律呼吸调节系统。这两个系统的下行通路是分开的。临床上有时可以观察到自主呼吸和随意呼吸分离的现象。例如在脊髓前外侧索下行的处主呼吸通路受损后，自主节律呼吸甚至停止，但病人仍可进行随意呼吸。患者靠随意呼吸或人工呼吸来维持肺通气，如未进行人工呼吸，一旦病人入睡，可能发生呼吸停止。
        <br> （二）呼吸节律形成的假说
        <br> 呼吸节律是怎样产生的，尚未完全阐明，已提出多种假说，最为流行的是局部神经元回路反馈控制假说。
        <br> 中枢神经系统里有许多神经元没有长突起向远处投射，只有短突起在某一部位内形成局部神经元回路联系。回路内可经正反馈联系募集更多神经元兴奋，以延长兴奋时间或加强兴奋活动；也可以负反馈联系，以限制其活动时间或终止其活动。平静呼吸时，由于吸气是主动的，所以许多学者更多地是去研究吸气中如何发生的，又如何转变为呼气的。有人提出中枢吸气活动发生器和吸气切断机制（inspiratory off-switch mechanism）的看法，认为在延髓有一个中枢吸气活动发生器，引发吸气神经元呈斜坡样渐增性放电，产生吸气；还有一个吸气切断机制，使吸气切断而发生呼气。在中枢吸气活动发生器作用下，吸气神经元兴奋，其兴奋传至①脊髓吸气肌运动神经元，引起吸气，肺扩张；②脑桥臂旁内侧核，加强其活动；③吸气切断机制，使之兴奋。吸气切断机制接受来自吸气神经元，脑桥背旁内侧核，和肺牵张感觉器的冲动。随着吸气相的进行，来自这三方面的冲动均逐渐增强，在吸气切断机制总合达到阈值时，吸气切断机制兴奋，发出冲动到中枢吸气活动发生器或吸气神经元，以负反馈形式终止其活动，吸气停止，转为呼气。切断迷走神经或毁损脑桥臂旁内侧核或两者，吸气切断机制达到阈值所需时间延长，吸气因面延长，呼吸变慢。因此，凡可影响中枢吸气活动发生器、吸气切断机制阈值或达到阈值所需时间的因素，都可影响呼吸过程和节律。
        <br> 二、呼吸的反射性调节
        <br> 呼吸节律虽然产生于脑，但其活动可受来自呼吸器官本身以及骨骼肌、其它器官系统感觉器‘传入冲动的反射性调节，下述其中的一些重要反射
        <br> （一）肺牵张反射
        <br> 1868年Breuer和Hering发现，在麻醉动物肺充气或肺扩张，则抑制吸气；肺放气或肺缩小，则引起吸气。切断迷走神经，上述反应消失，所以是反射性反应。由肺扩张或肺缩小引起的吸气抑制或兴奋的反射为黑-伯反射（Hering-Breuer reflex）或肺牵张反射。它有两种成分：肺扩张反射和肺缩小反射。
        <br> 1．肺扩张反射 是肺充气或扩张时抑制吸气的反射。感觉器位于从气管到细支气管的平滑肌中，是牵张感受器，阈值低，适应慢。当肺扩张牵拉呼吸道，使之也扩张时，感觉器兴奋，冲动经迷走神经走神经粗纤维传入延髓。在延髓内通过一定的神经联系使吸气切断机制兴奋，切断吸气，转入呼气。这样便加速了吸气和呼气的交替，使呼吸频率增加。所以切断迷走神经后，吸气延长、加深，呼吸变得深而慢。
        <br> 有人比较了8种动物的肺扩张反射，发现有种属差异，兔的最强，人的最弱。在人体，当潮气量增加至800ml以上时，才能引起肺扩张反射，可能是由于人体肺扩张反射的中枢阈值较高所致。所以，平静呼吸时，肺扩张反射不参与人的呼吸调节。但在初生婴儿，存在这一反射，大约在出生4-5天后，反射就显著减弱。病理情况下，肺顺应性降低，肺扩张时使气道扩张较大，刺激较强，可以引起该反射，使呼吸变浅变快。
        <br> 2．肺缩小反射 是肺缩小时引起吸气的反射。感受器同样位于气道平滑肌内，但其性质尚不十分清楚。肺缩小反向在较强的缩肺时才出现，它在平静呼吸调节中意义不大，但对阻止呼气过深和肺不张等可能起一定作用。
        <br> （二）呼吸肌本体感受性反射
        <br> 肌梭和腱器官是骨骼肌的本体感受器，它们所引起的反射为本体感受性反射。如肌梭受到牵张刺激时可以反射性地引起受刺激肌梭所在肌的收缩，为牵张反射，属本体感受性反射。呼吸肌也有牵张反射的主要依据是：在麻醉猫，切断双侧迷走神经，颈7横断脊髓，牵拉膈肌，膈肌肌电活动啬；切断动物的胸脊神经背根，呼吸运动减弱；人类为治病需要曾做类似手术，术后相应呼吸肌的活动发生可恢复的或可部分恢复的减弱。说明呼吸肌本体感受性反射参与正常呼吸运动的调节，在呼吸肌负荷改变时将发挥更大的作用。但是，这些依据不是无懈可击的。因为背根切断术不仅切断了本本感受器的传入纤维，也切断了所有经背根传入的其它感受器的传入纤维。近来的研究表明来自呼吸肌其它感受器的传入冲动也可反射性地影响呼吸。因此，对呼吸肌本体感受性反射应做更深更深入细致的研究，如研究分别兴奋不同感受器或传入纤维时对呼吸的效应。
        <br> （三）防御性呼吸反射
        <br> 在整个呼吸道都存在着感受器，它们是分布在粘膜上皮的迷走传入神经末梢，受到机械或化学刺激时，引起防御性呼吸反射，以清除激惹物，避免其进入肺泡。
        <br> 1．咳嗽反射 是常见的重要防御反射。它的感受器位于喉、气管和支气管的粘膜。大支气管以上部位的感受器对机械刺激敏感，二级支气管以下部位的对化学刺激敏感。传入冲动经迷走神经传入延髓，触发一系列协调的反射反应，引起咳嗽反射。
        <br> 咳嗽时，先是短促或深吸气，接着声门紧闭，呼气肌强烈收缩，肺内压和胸膜腔内压急速上升，然后声门突然打开，由于气压差极大，气体更以极高的速度从肺内冲出，将呼吸道内异物或分泌物排出。剧烈咳嗽时，因胸膜腔内压显著升高，可阻碍静脉因流，使静脉压和脑脊液压升高。 2．喷嚏反射 是和咳嗽类似的反射，不同的是：刺激作用于鼻粘膜感受器，传入神经是三叉神经，反射效应是腭垂下降，舌压向软腭，而不是声门关闭，呼出气主要从鼻腔喷出，以清除鼻腔中的刺激物。
        <br> （四）肺毛细血管旁（J-）感受器引起的呼吸反射
        <br> J-感受器位于肺泡毛细血管旁，在肺毛细血管充血、肺泡壁间质积液时受到刺激，冲动经迷走神经无髓C纤维传入延髓，引起反射性呼吸暂停，继以浅快呼吸，血压降低，心率减慢。J-感受器在呼吸调节中的作用尚不清楚，可能与运动时呼吸加快作肺充血、肺水肿时的急促呼吸有关。
        <br> （五）某些穴位刺激的呼吸效应
        <br> 针刺人中窕可以急救全麻手术过程中出现的呼吸停止。针刺动物人中可以使膈肌呼吸运动增强，电刺激家兔人中对膈神经和管髓呼吸神经元电活动有特异性影响。有人观察到在麻醉意外事件发生呼吸暂停时，刺激素可以兴奋呼吸。穴位的呼吸效应及其机制值得探讨。
        <br> （六）血压对呼吸的影响
        <br> 血压大幅度变化时可以反射性地影响呼吸，血压升高，呼吸减弱减慢；血压降低，呼吸加强加快。
