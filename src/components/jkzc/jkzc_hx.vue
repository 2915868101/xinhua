        肺通气使肺泡不断更新，保持了肺泡气PO2、PCO2的相对稳定，这是气体交换得以顺利进行的前提。气体交换包括肺与外界的气体交换体内的气体交换（肺换气和组织换气），前者是通过呼吸运动完成的，后者在这两处换气的原理一样，都是通过气体的扩散作用实现的。
        <br> 气体交换原理
        <br> （一）呼吸运动
        <br> 吸气时胸廓扩大，呼气时胸廓缩小。胸廓有节律地扩大和缩小，从而完成吸气与呼气，这就是呼吸运动。
        <br> 参与呼吸的肌肉有肋间肌和膈肌。肋间肌和膈肌的收缩与舒张能够使胸廓扩大或缩小。当肋间肌和膈肌收缩时，胸腔体积增大，肺随之扩张，这时肺内气压就低于大气压，外界空气通过呼吸道进入肺，完成吸气。相反，当肋间肌和膈肌舒张是时，胸腔体积缩小，肺随之回缩，这时肺内气压就高于大气压，肺内气体提高呼吸道排出体外，完成呼气。通过呼吸运动，肺实现了雨外界环境的气体交换，使肺泡内的气体不断地得到更新。
        <br> （二）气体的扩散
        <br> 气体分子不停地进行着无定向的运动，其结果是气体分子从分压高处向分压低处发生净转移，这一过程称为气体扩散，于是各处气体分压趋于相等。机体内的气体交换就是以扩散方式进行的。单位时间内氧化扩散的容积为气体扩散速率（diffusion rateD），它受下列因素的影响。 1．气体的分压差 在混合气体中，每种气体分子运动所产生的压力为各该气体的分压，它不受其它气体或其分压存在的影响，在温度恒定时，每一气体的分压只决定于它自身的浓度。混合气的总压力等于各气体分压之和。
        <br> 气体分压可按下式计算：
        <br> 气体分压=总压力×该气体的容积百分比
        <br> 两个区域之间的分压差（△P）是气体扩散的动力，分压差大，扩散快。
        <br> 2．气体的分子量和溶解度质量轻的气体扩散较快。在相同条件下，各气体扩散速率和各气体分子量（MW）的平方根成反比。溶解度（S）是单位分压下溶解于单位容积的溶液中的气体的量。一般以1个大气压，38℃时，100ml液体中溶解的气体的ml数来表示。溶解度与分子量平方根之比（S/***）为扩散系数（diffusion coefficient），取决于气体分子本身的特性。CO2的扩散系数是O2的20倍，主要是因为CO2在血浆中的溶解度（51.5）约为O2的（2.14）24倍的缘故，虽然CO2的分子量（44）略大于O2的（32）。
