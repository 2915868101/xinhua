        有氧呼吸：C6H12O6+6H2O+6O2——→6CO2+12H2O+能量（大多数生物）
        <br> 无氧呼吸
        <br> 瑜伽呼吸法
        <br> ：C6H12O6—→2C2H5OH+2CO2+能量（多数高等植物无氧呼吸的方式，酵母菌等）
        <br> 或者是：C6H12O6—→2C3H6O3+能量（动物、乳酸菌，马铃薯的块茎、甜菜的块根、玉米的胚等）
        <br> 此词是用于描述相互具有密切联系，但在意义上多少有所不同的一些现象。如：（1）二呼吸运动。（2）动物通过呼吸运动，从外界吸入分子态氧和向外界放出二氧化碳而进行气体交换的现象，这称为外呼吸。（3）细胞吸取氧和放出二氧化碳的现象，称为细胞呼吸。许多动物，通过外呼吸吸入的氧要运输到体内细胞和组织中消耗，所以，这与外呼吸相对应，而称为内呼吸。（4）作为细胞呼吸基础的生物化学变化，即呼吸代谢。可从狭义到广义分为几个阶段：（a）最狭义的是指在分子态氧的参与下，有机物完全分解成二氧化碳、水、氨等无机物，并供给能量，生物体可利用形态的能量（主要为ATP）。（b）包含前述（a）在内，其最常用的定义是细胞吸收分子态氧，而产生所有氧化过程。在这种意义上，呼吸强度是以单位时间内一定单位量的物质所吸收氧的容积来表示，通常是以QO2 来表示。当以有机物作为呼吸基质（呼吸材料）时；将二氧化碳放出量和氧的吸收量的容积比称为呼吸商（RQ）。（c）将前述（b）的定义进一步扩大，包扩在无氧条件下，以硝酸盐、硫酸盐等取代氧，作为最终电子受体进行氧化而获得能量的过程，这些过程分别称为硝酸呼吸、硫酸呼吸。（d）最广的定义是指化学的暗反应，凡通过氧化还原获得能量的过程，都称为呼吸。此时，氧成为最终电子受体，而将上述b的过程特称为有氧吁吸或好氧呼吸，而其他则称为无氧呼吸、或厌氧呼吸。酒精发酵、糖酵解也属于这种意义上的无氧呼吸。发酵，是在基质产生的化合物间
        <br>进行氧化还原反应、此时，可称为分子内呼吸。一般把呼吸机制看作是有机质的变化和脱氢反应中的电子传递过程。
        <br> 正常呼吸:呼吸与脉搏的比例为1:4,男性及儿童以腹式呼吸为主,女性以胸式呼吸为主。
