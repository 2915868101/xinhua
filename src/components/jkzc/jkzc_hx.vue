        <br> 气体在肺的交换
        <br> （一）交换过程
        <br> 混合静脉血流经肺毛细血管时，血液PCO2是5.32kPa(40mmHg)，比肺泡气的13.83kPa(104mmHg)低，肺泡气中O2便由于分压的差向血液扩散，血液的PCO2便逐渐上升，最后接近肺泡气的PCO2。CO2则向相反的方向扩散，从血液到肺泡，因为混合静脉血的PCO2是6.12kPa(46mmHg)，肺泡的PCO2是5.32kPa(40mmHg)。O2和CO2的扩散都极为迅速，仅需约0.3s即可达到平衡。通常情况下血液流经肺毛细血管的时间约0.7s，所以当血液流经肺毛细血管全长约1/3时，已经基本上完成交换过程。可见，通常情况下肺换气时间绰绰有余。
        <br> （二）影响肺部气体交换的因素
        <br> 前面已经提到气体扩散速率受分压差、扩散面积、扩散距离、温度和扩散系数的影响。这里只需具体说明肺的扩散距离和扩散面积以及影响肺部气体交换的其它因素，即通气/血流比值的影响。
        <br> 1．呼吸膜的厚度 在肺部肺泡气通过呼吸膜（肺泡-毛细血管膜）与血液气体进行交换。气体扩散速率与呼吸膜厚度成反比关系，膜越厚，单位时间内交换的气体量就越少。呼吸膜由六层结构组成；含表面活性物质的极薄的液体层、很薄的肺泡上皮细胞层、上皮基底膜、肺泡上皮和毛细血管膜之间很小的间隙、毛细血管的基膜和毛细血管内皮细胞层。虽然呼吸膜有六层结构，但却很薄，总厚度不到1μm，有的部位只有0.2μm，气体易于扩散通过。此外，因为呼吸膜的面积极大，肺毛细血管总血量不多，只60-140ml，这样少的血液分布于这样大的面积，所以血液层很薄。肺毛细血管平均直径不足8μm，因此，红细胞膜通常能接触至毛细血管壁，所以O2、CO2不必经过大量的血浆层就可到达红细胞或进入肺泡，扩散距离短，交换速度快。病理情况下，任何使呼吸膜增厚或扩散距离增加的疾病，都会降低扩散速率，减少扩散量，如肺纤维化、肺水肿等，可出现低氧血症；特别是运动时，由于血流加速，缩短了气体在肺部的交换时间，这时呼吸膜的厚度和扩散距离的改变显得更有重要性。
        <br> 2．呼吸膜的面积 气体扩散速率与扩散面积成正比。正常成人肺有3亿左右的肺泡，总扩散面积约70m2。安静状态下，呼吸膜的扩散面积约40m2,故有相当大的贮备面积。运动时，因肺毛细血管开放数量和开放程度的增加，扩散面积也大大增大。肺不张、肺实变、肺气肿或肺毛细血管关闭和阻塞均使呼吸膜扩散面积减小。
        <br> 3．通气/血流比值的影响通气/血流比值（ventilation/perfusion ratio）是指每分肺通气量（VA）和每分肺血流量（Q）之间的比值（VA/Q），正常成年人安静时约为4.2/5=0.84。不难理解，只有适宜的VA/Q才能实现适宜的气体交换，这是因为肺部的气体交换依赖于两个泵协调工作。一个是气历史意义，使肺泡通气，肺泡气得以不断更新，提供O2，排出CO2；一个是血泵，向肺循环泵入相应的血流量，及时带起摄取的O2，带来机体产生的CO2。如果VA/Q比值增大，这就意味着通气过剩，血流不足，部分肺泡气未能与血液气充分交换，致使肺泡无效腔增大。反之，VA/Q下降，则意味着通气不足，血流过剩，部分血液流经通气不良的肺泡，混合静脉血中的气体未能得到充分更新，未能成为动脉血就流回了心脏。犹如发生了动-静脉短路，只不过是功能性的而不是解剖结构所造成的动-静脉短路。由此可见，VA/Q增大，肺泡无效腔增加；VA/Q减小，发生功能性动-静脉短路。两者都妨碍了有效的气体交换，可导致血液缺O2或CO2潴留，但主要是血液缺O2。这是因为，动、静脉血液之间O2分压差远远大于CO2的，所以动-静脉短路时，动脉血PO2下降的程度大于PCO2升高的程度；CO2的扩散系数是O2的20倍，所以CO2的扩散较O2为快，不易潴留；动脉血PO2下降和PCO2升高，可以刺激呼吸，增加肺泡通气量，有助于CO2的排出，却几乎无助于O2摄取，这是由氧离曲线和CO2解离曲线的特点所决定的。肺气肿病人，因许多细支气管阻塞和肺泡壁的破坏，上述两种VA/Q异常都可以存在，致使肺换气速率受到极大损害，这是造成肺换气功能异常最常见的一种疾病。
        <br> 健康成人就整个肺而言VA/Q是0.84。但是肺内肺泡通气量和肺毛细血管血流量的分布不是很均匀的，因此，各个局部的通气/血流比值也不相同，用整个肺的VA/Q就不以反映出来。例如人友直立位时，由于重力等因素的作用，肺尖部的通气和血流都较肺底的小，不过血流量的减少更为显著，所以肺尖部的通气/血流比值增大，产生中度肺泡无效腔，而肺底的比值减小，产生功能性动-静脉短路。虽然正常情况下存在着肺泡通气和血流的不均匀分布，但从总体上说，由于呼吸膜面积远远超过气体交换的实际需要，所以并未明显影响O2的摄取和CO2的排出。
