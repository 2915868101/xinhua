        概述
        <br> 呼吸是指机体与外界环境之间气体交换的过程。人的呼吸过程包括三个互相联系的环节：外呼吸，包括肺通气和肺换气；气体在血液中的运输；内呼吸，指组织细胞与血液间的气体交换。正常成人安静时呼吸一次为6.4秒为最佳，每次吸入和呼出的气体量大约为500毫升，称为潮气量。当人用力吸气，一直到不能再吸的时候为止；然后再用力呼气，一直呼到不能再呼的时候为止，这时呼出的气体量称为肺活量。正常成人男子肺活量约为3500-4000毫升，女子约为2500-3500毫升。
