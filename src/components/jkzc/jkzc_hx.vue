      <p class="f-tit">
        <span>肺通气</span>
      </p>
      <p class="f-cont">
        气体进入肺取决于两方面因素的相互作用：一是推动气体流动的动力；一是阻止其流动的阻力。前者必须克服后者，方能实现肺通气，正如心室射血的动力必须克服循环系统的阻力才能推动血液流动一样。
        <br> （一）肺通气的动力
        <br> 气体进出肺是由大气和肺泡气之间存在着压力差的缘故。在自然呼吸条件下，此压力差产生于肺的张缩所引起的肺容积的变化。可是肺本身不具有主动张缩的能力，它的张缩是由胸廓的扩大和缩小所引起，而胸廓的扩大和缩小又是由呼吸肌的收缩和舒张所引起。当吸气肌收缩时，胸廓扩大，肺随之扩张，肺容积增大，肺内压暂时下降并低于大气压，空气就顺此压差而进入肺，造成吸气（inspiration）。反之，当吸气肌舒张和（或）呼气肌收缩时，胸廓缩小，肺也随之缩小，肺容积减小，肺内压暂时升高并高于大气压，肺内气便顺此压差流出肺，造成呼气（expiration）。呼吸肌收缩、舒张所造成的胸廓的扩大和缩小，称为呼吸运动。呼吸运动是肺通气的原动力。
        <br> （二）肺通气的阻力
        <br> 肺通气的动力需要克服肺通气的阻力方能实现肺通气。阻力增高是临床上肺通0气障碍最常见的原因。肺通气的阻力有两种：弹性阻力（肺和胸廓的弹性阻力），是平静呼吸时主要阻力，约占总阻力的70%；非弹性阻力，包括气道阻力，惯性阻力和组织的粘滞阻力，约占总阻力的30%，其中又以气道阻力为主。
        <br> （三）呼吸功
        <br> 在呼吸过程中，呼吸肌为克服弹性阻力和右面弹性阻力而实现肺通气所作的功为呼吸功。通常以单位时间内压力变化乘以容积变化来计算，单位是kg·m。正常人平静呼吸时，呼吸功不大，每分钟约为0.3-0.6kg·m,其中2/3用来克服弹性阻力，1/3用来克服非弹性阻力。劳动或运动时，呼吸频率、深度增加，呼气也有主动成分的参与，呼吸功可增至10kg·m。病理情况下，弹性或非弹性阻力增大时，也可使呼吸功增大。平静呼吸时，呼吸耗能仅占全身耗能的3%。剧烈运动时，呼吸耗能可升高25倍，但由于全身总耗能也增大15-20倍，所以呼吸耗能仍只占总耗能的3%-4%。
