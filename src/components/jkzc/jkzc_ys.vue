        <span>中国的饮食</span>
      </p>
      <p class="f-cont">
        在世界上是享有盛誉的，华侨和华裔外籍人在海外谋取生，经营最为普遍的产业就是餐饮业．有华人处应有中国餐馆，中国的饮食可以说是“食”被天下．这一现象早在本世纪初时，就被革命的先行者孙中山先生敏锐地观察到了。孙中山先生在其《建国方略》一书中说：“我中国近代文明进化，事事皆落人之后，惟饮食一道之进步，至今尚为各国所不及。”孙中山先生的这一论述十分正确，而事实上确如孙中山先生所言，中华文明到了近代，被西方文明冲击得七零八落，有识之士莫不诊脉问疾。然而中国餐馆则大异其趣，居然能够登陆欧美，遍布全球，所向披靡，至今世界上几乎每一个角落都有中餐馆。这种强烈的反差引起了学术界的关注，但是并没有一个较有说服力的解释。我们认为，中国餐馆能够遍布世界各地不是一个孤立和偶然的现象，其中的原因与中国饮食文化有着千丝万缕的关联。因而探讨一下中国饮食文化的某些特征，将有利于我们对于这一现象的解释。
        <br> 一谈到中国饮食文化，许多人会对中国食谱以及中国菜的色、香、味、形赞不绝口。但是如果要从比较的角度来探讨饮食文化，可以操作的办法是把握住中国饮食文化的精髓。没有比较就没有鉴别。笔者在这里认为，比较可行的办法是在讨论中国饮食文化时，从饮食生活方式的角度来着手。而实际上，中国饮食文化，实际上也是指中国人的饮食生活方式。而要谈中国饮食文化就必须涉及到中国文化，两者的关联是极其密切的。因而我们有必要先对中国文化的某些特征作一阐述。
        <br> 中国文化是一个什么样的文化呢？这必须了解一下早期中国文化的地理环境，因为越是在早期，文化受环境的制约越大。中国位于亚洲大陆的东南部，西北面是茫茫的沙漠、草原和戈壁，东南则是茫茫的大海。中国文化的主要温床黄河流域的土地十分虽然较肥沃，但是其它环境较恶劣，多风少雨，生存的条件亲不好。文明产生以后，当时的文化就以比较强烈的现实主义的思潮表现出来，伦理道德的色彩十分浓厚。因为这个社会需要谦让需要少欲，而一个社会需要什么的时候，往往是她缺少什么的时候。中国文化从一开始就表现出早熟的特征。马克思就曾经认为，中国文明是一个早熟的婴儿，她的发育过程中具有明显的超前特征。反观西方文明，则大相径庭。西方文化的童年时代的希腊文明，以其独有的浪温气息写入文明史，马克思曾经认为希腊文明是一个发育正常的婴儿。中国文化的生存土壤，在其发展过程中逐渐干旱，逐渐变得不得于农业的生产，由于土壤及其它因素等十分不利于农业生产，现实的苦难产生不了文化的浪漫，因而中国文明以其强烈的道德和禁欲特征贯穿始终。先秦时期诸子百家相互诘难，殚精竭力却殊途同归，目的是建立一个没有争斗的理想世界。但这只是理想而已。战国之际，列国纷争，以实力相较，最终秦国因鼓励军功，奖励耕战而一统天下，这是人心所向。天下纷争，百姓如何生活，这也是历史的选择。只有一统，才不至于“天下几人称王，几人称帝。”？（曹操语）
        <br> 先秦时期，中国南方楚国也是曾流露出浪漫主义的思潮，但是很快被北方现实主义的思潮所笼罩和淹没，秦始皇统一中国以后，书同文，以后的历史就是天子一统的历史。中国的封建社会为了维护这一统的局面，允许君王具有至高无上的权威，但同时赋予他许多封建礼仪进行约束；中国有世界上最早的公平取官的文官制度——科举制；中国历史上呼唤清官，一旦出现便歌之颂之，并为之产生崇拜。所以中国尽管没有严格意义上的宗教，但是其某些特征与宗教的特征有相似性，也有它自己的神；如果把这一现象也视为宗教的话，那么中国式的宗教则是在现实意义上的极端的理想主义。中国历史上的极度理想主义也因之产生，它也有神，只不过其所造之神是以现实中的人为化身的，而现实中的人是不可能充当理想中的神的，所以中国历史上造神运动层出不穷，直至今天。或者说中国人的宗教是一种多神崇拜，而不是一神崇拜，什么财神、药神、灶神、妈祖等，都被视为神而被崇拜。
        <br> 中国文化还具有另外的两大特征：其一是绝对地追求适用，而杜绝一切的不实用的东西。从中国的科技史的历程中可以看到，历史上，所有的实用的技术十分发达，如农业科技和医学十分发达，而有关的非实用的一些东西则无法发展。先秦时期墨家的有关光学和几何学的知识，在汉代独尊儒术以后，没有能够继续发展下去。中国没有产生物理学、化学、生物学等基础学科。当然炼丹术在皇帝追求长生不老的过程中也很发达，其中确实包含有化学方面的知识。中国的手工业十分发达，而近代机器大工业生产，如蒸汽机没有在中国被发明。十五世纪以前，世界上绝大部分发明是在中国人手中产生的，但是近代以后需要通过一系列实验手段才能产生的近代科学未能在中国产生：中国文化表现出特有的仁爱特征。孔子的思想的核心是“仁者爱人”，因为仁慈，中国人实施多子继承制度，这样财产一方面得以析散，另一方面限制了远距离的迁徙，中国人安土重迁与之关系重大。这一继承制度限制了商业的发展，从而便多子多福的思想得以产生，多子多福的思想使社会的人口增加，派生出更多的多子多福，社会人口压力始终存在，只要社会的生产力能够养活多少人口，就会有多少人口出现。
        <br> 中国饮食文化的诸多特征，体现在饮食文化之上，直接影响着中国饮食文化的发展。首先因为人口压力以及其它多种原因的存在，所以中国人的饮食从先秦开始，就是以谷物为主，肉少粮多，辅以菜蔬，这就是典型的饭菜结构。其中饭是主食，而菜则是为了下饭，即助饭下咽。为什么要助饭下咽呢？那是因为主食并不可口，必须有一种物质来辅助它使人们能够吃下去。当然菜的功能和盐的功能有共同点，但是不等同于盐。这样促使中国的烹饪的首要目的是装点饮食，使不可口的食物变得精妙绝伦；其次，由于中国文化追求衫，中国手工业的发达，所以烹饪中的饮食加工技术在世界上是首屈一指，体现了中国文化的衫特征。所有这些，使中国饮食文化有如下特征。其一，中国烹饪技术发达，许多西方人看来不可食的物品，经过中国厨师的劳作，变得使人一见而食欲顿开；其二，中国人的食谱广泛，举凡能够食者皆食，毫无禁忌；其三，中国救荒的著述特别多，以备饥荒之年以野菜充饥之用；其四，中国人将食的追求作为人生至乐来追求，吃饭成为第一要求。由于中国人在吃的方面不能够随心所欲，有诗为证：
        <br> 红日巡天过午迟，腹中虚实自家知。
        <br> 人生一饱非难事，仅在风调雨顺时。
        <br> 因此长期以来吃穿不愁难以办到，所以吃在中国人的生活中占有特殊的位置，证据不胜枚举。
        <br> 此外，我们可以对西方饮食文化的某些特征加以分析，也许能够加深对中国饮食文化的理解。西方的饮食，由于最初是主要以畜牧为主，肉食在饮食听比例一直很高，到了近代，种植业比重增加，但是肉食在饮食中的比例仍然要比中国人的高。由于肉食的天然可口，所以西方人没有必要对饮食进行装点，内食的天然可口限制了烹饪的发展，欧洲人在显示富裕的时候，多是饮食的工具来表现，如各种的器皿的多少和豪华成为讲究的内容。另外也不把吃这种人生易于满足的事情作为人生的至乐来追求。西方人的人生追求，正好同一句中国的俗语语可以高度概括，即“饱暧思淫欲”。他们更多地追求情爱，西方文化体现出一种“情爱文化”，而中国文化则体现出一种“吃的文化”。
        <br> 但是我们还要指出的是，中国饮食的诸多特征，是在老百姓日常生活中看不到的。宫庭饮食，市肆饮食则能够更好、更全面地表现出这些特征。平民的节日饮食，如春节，也许能够部分地表现出这些特征来，但是春节对于一年的365天来说毕竟是短暂的一瞬间，如昙花一现。
        <br> 文化无优劣，饮食文化也无优劣，其区别只不过是不同的环境条件下的生活方式或者饮食方式的不同罢了。但是如何理解和分析这一区别则是非常重要的。本书的目的基于这一点而作一点尝试，是否能够得到大多数读者的认可不得而知。在大多数情况下下本人属于那种“远庖厨”者，并不懂得烹饪，也不是美食家，但是本人深知“一饭一粥当思来之不易”，在此意义上，把我对于中国饮食文化的某些看法，行之于笔端，希望得到方家的指教。
