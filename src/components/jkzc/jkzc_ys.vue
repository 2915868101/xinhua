        <span>现代人饮食的六大陋习</span>
      </p>
      <p class="f-cont">
        吃是一种享受，但是您可曾想过不正确的饮食方式会导致诸多慢性病如糖尿病、高血压、心血管性疾病甚至癌症的发生。饮食习惯最重要的是不可偏离大自然的原则。
        <br> 现代人饮食第一个主要问题是热量的过剩及不均衡。
        <br> 各位可曾看过在非洲大草原上有过度肥胖而无法跑步的狮子？除了人类以外，所有动物都是肚子饥饿的时候才出去觅食。过量摄取食物，尤其是肉类的过量，已经是人类成人病及癌症增加的主要原因。那么该如何吃才是适量呢？
        <br> 首先，请先照照镜子看看自己的牙齿，人类选择食物的内容应该与牙齿的结构相符合。健康的人类有32颗牙齿，上下左右相对称。如果只有看上排的牙齿，左右各有两颗门牙、一颗犬齿、五颗臼齿。各位都知道，门牙主要是用来啃食蔬菜及水果，犬齿是用来撕碎肉类，而臼齿是用来咀嚼谷类及豆类等食物。由此可知，人类饮食的内容应以谷类及豆类为主食，蔬菜及水果为副食，而肉类则为次副食其比例应为5比2比1最为合适。那么究竟是吃多少量才好呢？其实应该是根据每个人的胃部感觉为主要判断。中国人讲：吃饭八分饱，也就是说只要根据上述饮食的比例，吃到肚子里觉得已有八分饱时，就应适可而止。如此就不会有营养过剩或营养不均衡的问题。或许有些人会怀疑上述的饮食比例会不会导致蛋白质的缺乏。事实上，谷类及豆类除了含有丰富的淀粉外，也含有易被消化吸收的蛋白质。虽然它们个别都是不完全蛋白质（各缺乏某些必须胺基酸），但若一起食用则可达互补作用，而成为完全蛋白质，其营养价值绝不输给肉类的蛋白质。
        <br> 现代人的第二个违反大自然法则的饮食方式是烹调方式的错误。
        <br> 最近快餐店蓬勃发展，如麦当劳、肯德基等店面的广设，使炸鸡、炸薯条成为小朋友们的最爱。很多人以为油炸食品用的是植物油而非猪油或牛油应该不会有问题。事实上，植物油多为不饱和脂肪酸很容易在高热下被氧化产生脂质自由基及过氧化脂质等有害物质。这些氧化物质不仅会促进动脉硬化、黑斑、皱纹等老化现象的进行，也容易诱导癌症（尤其是肺癌）的发生。因此，调理的方式最好以炖、卤、水炒的方式，而少用油炸或油煎的方式。
        <br> 现代人第三个违反大自然法则的饮食方式是食用过度精制及缺乏纤维的食物。
        <br> 如糙米的胚芽中原就含有很丰富的维他命及一些抗氧化成份，但人类却在加工过程中将胚芽舍弃，而只吃只含淀粉及少量蛋白质的白米部份。大豆原就含有丰富的蛋白质及一些具抗氧化活性的类黄酮类物质。但我们在加工制造豆浆的过程中，却将豆渣去掉，也就把大部份蛋白质及结合在蛋白质上的类黄酮类抛弃掉了，真是暴殄天物。谷类及豆类中也含有丰富的纤维素，纤维素不但可以促进大肠的蠕动，帮助人体排出胆固醇及致癌毒素，而且可以促进肠内有益菌（如乳酸菌）的成长。但在加工过程中，谷类及豆类的纤维素也同时会被抛弃掉了。
        <br> 现代人饮食第四个问题是食物污染的问题。
        <br> 为了让蔬菜、水果不被虫害，农夫大量喷农药。为了让牲畜不被细菌感染，养猪或养鸡户给猪只或鸡只大量注射抗生素。再加上环境的污染，干净的食物已非常难得，食物污染的问题已严重威胁到人类的健康。我们很高兴看到生机饮食的理念逐渐盛行，强调食用不被污染、不用化学肥料的天然食品之观念逐渐被民众所接受。很多的农场也开始大量栽培有机天然蔬果。黄绿色蔬菜水果含有丰富的维他命、微量元素及纤维素。多吃黄、绿色蔬菜及水果已被发现可以预防成人病的进行也可以减少癌症的发生。但是唯有采用清洁、安全可靠的食物来源及正确的烹调方式才能将这些老天爷赐给我们的礼物完全保留。
        <br> 现代人饮食第五个问题是没有用心的去吃东西。
        <br> 我们常常很紧张的吃东西或在诸多挂虑下吃东西。在这种状况下消化道的唾液及胃液并未充分的分泌，也就不能好好地消化食物。吃东西是一种享受，古代人讲："吃饭皇帝大"。当我们吃东西时就要有像当皇帝一样的感觉，天大地大的事都不要去想它。各位可以想象在大草原上吃草的马儿是多么的悠闲自在。我们要用敏感的嗅觉去闻闻食物的香味，用色咪咪的眼光去欣赏食物的美色，用敏锐的舌头去品尝食物的天然美味，用轻松愉快的心情慢慢咀嚼食物，等待口中满是口水时再吞进食物。我们要以一颗感恩的心来吃每样上天赐给我们的美好食物，因为它们得之不易。您可以想象婴儿在母亲怀中吮吸母乳时那种安详和满足的模样，唯有在心中充满爱的情绪下，吾人的消化系统才能发挥最佳的功能。
        <br> 现代人饮食习惯第六个问题是缺乏运动。
        <br> 有人会问，怎么运动会和饮食有关？事实上运动会促进胃肠的蠕动，有助身体将脂肪或有害毒素排掉。很多人常抱怨经常便秘和腹胀，吃进去的东西好象不见了一样，此大多数是由于缺乏运动所造成。运动同时可以增加人体细胞的耗氧量，靠着氧气将葡萄糖或脂肪酸转变成ATP储存。当人体需要能量时，ATP很快可以转变为能量，此化学反应就好象蓄电池储存能量一样。若是运动不足则人体的葡萄糖或脂肪酸不能变化为ATP，反而转变为脂肪组织堆积在人体内。因此我们可以说：运动不足和过食一样，是造成肥胖的最主要原因。什么运动最合适呢？事实上以适合于自己体能状况之柔软性运动最理想，如慢跑、快走、骑脚踏车、跳韵律舞、游泳等。但必须运动至身体发汗、心跳加速达每分钟100次以上，并且持续10至20分钟以上的运动，才能发挥效果。运动应该和"吃"一样，被养成一个习惯而且持之有恒。
        <br> 唯有在愉快的心情下，适量而均衡地摄取正确调理的天然食物，并且配合充份的运动，才能吃出健康，吃出青春和美丽！
