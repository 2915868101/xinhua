      <p class="f-tit">
        <span>饮食十境界</span>
      </p>
      <p class="f-cont">
        境界一 “果腹”
        <br> 吃的第一大境界当然是“果腹”，俗话说就是填饱肚子，就是一个“吃”字。形式比较原始，只解决人的最基本的生理需要。这个境界的吃，不需要费心找地儿，各种商场的小吃城；街头的成都美食和中式的快餐店，如：各式面馆、永和豆浆等，西式快餐的麦当劳、肯德基也可列入其中，一盘宫保鸡丁，一盘白菜豆腐，外加一小碗汤，一碗主食足以。一个人，两个人，三五人均可。这个境界的吃千万别麻烦，一麻烦就脱离了本质，吃起来也就十分不爽。
        <br> 境界二“饕餮”
        <br> 吃的第二大境界应该是“饕餮”。吃的是一个“爽”字。呼三五好友去一家稍大众的馆子，稀里哗啦点上满满的一大桌菜，价钱却不贵，胡吃海塞一通，兴致所致，还能吆喝两声，划几下拳，甚至还可以赤膊上阵。适合这种吃法的有涮肉、大众菜等。但免不了一个“俗”字，有不雅之嫌疑，同时也有浪费之嫌疑。
        <br> 境界三“聚会”
        <br> 吃的第三大境界应该是“聚会”。此境界重在这个“聚”字。家人、朋友、加班聚餐等都属于这一类。这种吃不需要太多的讲究，“吃”是个形式，关键在“聚”背后的引申含义。逢年过节、生日聚会、升迁发奖，友人来访，随便找个理由都可以去趟馆子，这是一种礼节上的习惯。这种吃讲究个热闹。不需要太豪华和奢侈。金百万、金鼎轩、小土豆、稍微高档一点的川菜馆如巴国布衣等之类都可以满足需要，高档一点的还可以去吃一些比较流行和有名气的馆子。
        <br> 境界四“宴请”
        <br> 吃的第四大境界应该是“宴请”。多以招待为主。商场招待、官场招待、公务招待、上下级招待、危机处理招待、重大事情招待等都属于这一范畴。这种吃不以“吃”的本质为主旨，关键在于这个招待背后的目的。所以，这种吃重在讲究一个排场，价钱昂贵，因此也多以公款招待为主。在京城，这是支撑餐饮经济的重要支柱。这种吃都有一个共同点，大多都是在“包间”进行，所以，对馆子的要求要严格一点。如各大宾馆饭店的豪华餐厅，知名大酒楼等、海鲜、鱼翅、鲍鱼宴、官府菜等。但这个境界的吃难免给人一种有暴殄天物之叹，吃后回来，却又发现没有吃饱。
        <br> 境界五“养生”
        <br> 吃的第五大境界应该是“养生”。它比较讲究“食补”，是大吃大喝在认识观念上的一种理性升华。这种吃多以正宗的煲汤为主：甲鱼汤、老鸦汤、野山菌汤等，足足地焖上十多个小时，满满地端上来，味道纯正，饱饱地喝上一顿，无比滋润，真乃人生一大幸事，就是从心理上对积劳的身体也是一个安慰。在北京，这样的馆子有很多，进入饭店的正门，有一排瓷罐摆着，大多比较正宗。江西来的瓦罐、广东来的各式煲、云南来的野山菌汤、正宗的鱼翅煲、潮洲来的燕窝汤、官府菜的南瓜鱼翅都是比较典型的。但有时价钱却是一个瓶颈，不是一般人轻易就能上口的。
        <br> 境界六“解馋”
        <br> 吃的第六大境界应该是“解馋”，吃的东西一定要“鲜”。这个境界有两个层次：一是吃“物”，如那些只常听说却鲜见的鲜物鲍鱼、龙虾、法式大餐之类；二是吃“名”、吃“文化”：如大董和全聚德的烤鸭，顺峰、黎昌的海鲜以及淮扬菜、杭州菜、本帮菜、谭鱼头火锅、皇城老妈、孔乙己酒店等。 境界七“觅食”
        <br> 吃的第七大境界应该是“觅食”，那就得四处去“找”。在寻找中获得“吃”的乐趣，是本境界的一大妙处。这个“找”又分为两个层次，一个是有目的地去找口头盛传的流行馆子；一个是漫无目的地找寻意中的吃处。这个“找”字重在一个偶然发现，豁然开朗，一下子就对上了当时的心情和感觉。
        <br> 境界八“猎艳”
        <br> 吃的第八大境界应该是“猎艳”，所以馆子要“奇”。这样的馆子都是比较稀罕的特色店，如：非洲来的、越南来的等都在这一范畴。“新”、“奇”、“特”是主要特征。适合这类馆子的人群一般是时尚人士、有品位的少夫少妻、白领人群等。价钱中档，不用担心被宰。
        <br> 境界九“约会”
        <br> 吃的第九大境界应该是“约会”，这时吃的已经不是“物”，而是“情”。大多的时候，点的多，吃的少。这种吃千万不要是两个同性别的人，以免让人误会，最好也不要是夫妻，因为已过了“约会”的阶段。凡是到这种地方来吃的，两人之间大多都有一种心灵上的默契，说出来就变得俗，不表现出来又压抑。于是，以一个“吃”的借口“会”在一起，吃也吃了，谈也谈了，尽管大多的时候没有吃。适合这类吃的馆子如：茶餐厅、有餐饮服务的咖啡店，而且一定还要有柔软的沙发。
        <br> 境界十“独酌”
        <br> 吃的最后一个境界应该是“独酌”，在于一个“品”字，吃什么不太重要，关键是一个寥落的心情，要么伤感、要么闲适。这种馆子一定要是隐匿在很深的巷子里，店面古朴，又十分宁静。这个时候还应该有一瓶古典的酒，最好屋外再飘着零星的雨丝或雪花。一个人浅斟低酌，物我两忘。惟一的遗憾是，这种店已经很少有，恐怕只有在梦里才可以见得着。
        <br> 中国饮食博大精深，要想进一顿餐，就能在“文化”上有所斩获，不是件容易事。多数餐厅是在菜品的渊源上做文章，讲个典故似乎文化味就足了。记者见识了一家“寺院食府”，对这“境界”一词有了更深切体会。
        <br> 中餐的生命力
        <br> 英语里有句话，“You are what you eat”。这句话翻译的确不容易，可意思是说你吃什么就决定你是什么样的人。西方人这些年大讲饮食健康，甚至餐厅的菜单上都标明哪样菜是多少卡路里，可能根儿上与这句话有关。中国人也有句类似的话，“吃哪儿补哪儿”。以形补形，有点像中国的象形文字，产生的道理很直接。怕小孩不够聪明就让他吃动物脑子，见老人腿脚不好就让他吃猪蹄，看来想用吃什么来解决问题的想法在中国和西方是一样的。
        <br> 人们常说食文化，说哪个国家的饭好吃，哪个国家的饭不好吃，其实食文化的核心我看不在于吃什么，不在于吃的味觉，而在于它的吃法、吃的方式。在大多数地方的人解决了吃饱的问题后，吃越来越变成了一种社会活动的行为。不论什么食品，过到嘴里和肚子里之后都变成了生物学和营养学的问题，而在吃之前，则更能反映文化的习惯。
        <br> 就说餐厅里的点菜，西餐的菜单大多只有一页纸，最多是正反面，还包括了饮料和甜品，越是好的餐厅，大多菜单越短，就像西方文化，虽然也很丰实，很深刻，但它的集中度很高。中餐则不同，菜单一般都很长，选择很多，点菜变成了一件比较困难的事，是多样的融合，它的丰实表现在多样化上。中餐的前后道菜之间没有必然的联合，吃什么与喝什么酒水也没有必然的联系，很有些像中国多元的文化观念和不断改变的社会组织形式，多样、包容、同化、为我所用是基本特点。不过也有些国外的中餐馆把中餐改成了西餐的吃法，也把中餐分开了什么是头盘，什么是主菜，什么是餐后甜品，老外果然吃的不亦乐乎，既享用了中餐的美味，又在心理上觉得顺序很舒服，看来文化融合的结果是好的。
        <br> 西餐是每个人点自己的菜，中餐是主人把菜都点好大家只管吃，两者各有千秋，自己点菜对个人选择更尊重一些，自己点，好坏自己负责，但品种一定单一，吃起来也没有那么热闹，但看出来西方文化中个体的独立性。中国文化中更多的重视整体，点菜的人要满足不同人的口味，可能会受到赞扬，也可能会受到批评，所以中国有句话叫做众口难调，也知道这是件难事，可还要这样做，因为觉得整体的一致更重要。中餐讲究分享，可能也是不患寡患不均的影响，这种分享的渗透之深，使中国人在吃西餐的时候也分享，即使大家都各自点了菜，也会在吃的时候你尝点我的，我尝点你的，搞得餐桌上还是很热闹，一派乐融融的气氛。
        <br> 中西餐吃法在餐桌上不同，在厨房里也很不一样。我看过华润在曼谷的酒店的中西厨房，感觉文化的对比也很明显。西餐的厨房更像一间工厂，有很多标准设备，有很多计量、温度、时间的控制，厨房的布局也是按流程设计的，有对出品的样式、颜色的严格要求。其实老外的脑子都比较简单，但他们把很多事标准化了、程序化了，就提高了做事的效率和连贯性，麦当劳和星巴克都是产品标准化的结果。中餐的厨房则很不同，推门进去，火光冲天，厨师一排，都满头大汗，各种调料摆了几排，最终的好不好吃，要看厨师手中的大勺子怎么掌握了。所以中餐在源头上就难统一标准，更多的要靠厨师的个人技术，否则就不是中餐了。这可能与中国文化很多事都要靠某个人的行为才能完成是一样的。不过好多事都不可能，也没有必要全一致起来，中餐的生命力可能就是来自于它的不统一，否则这个世界就太单调了。
        <br> 西方人吃饭用刀叉，有人说现在还用刀子来切带血的牛肉的民族一定是文明进化得不够，不过因为刀叉很实用，老外也没有改的愿望。不论西方文明再怎么进步，刀叉看来改不了。中国人吃饭用筷子，老外时常惊奇中国人怎么能灵巧地用筷子夹起盘中软软的豆腐和滑滑的花生米。刀叉和筷子时常是餐桌上的话题，这几年不会用刀叉的中国人少了，可不会用筷子的老外还不少，可见中国人吸收学习的能力很强，这种吸收和学习的精神可能是中国文化中最宝贵的部分。
