      <p class="f-cont">
        油条,火锅,羊肉串,酸菜鱼——你知道有害食物的“解药”吗？
        <br> 有害食物的“解药”
        <br> 如何改善饮食方式，使摄入的食物对健康更有利，已得到人们的普遍关注。不过，如果你在难以抵挡某些有害食物的诱惑后，采取一些适当的方法，同样也能降低它对你的危害。
        <br> 状态1：油条
        <br> 油条是不少人早餐的选择，可油条中大多加明矾。这种含铝的无机物，被人体吸收后会对大脑神经细胞产生损害，并且很难被人体排出而逐渐蓄积。长久对身体造成的危害是，记忆力减退、抑郁和烦躁，严重的可导致"老年性痴呆"等可怕疾病。
        <br> 解物：豆浆。如果你在食用油条时，佐以豆浆，那么就在无意中保护了自己。因为豆浆中富含卵磷脂。科学家发现给老年性痴呆患者服用一定剂量的卵磷脂，可以使他们的记忆力得到一定的好转。
        <br> 状态2："酸菜鱼"
        <br> 酸菜鱼是人们喜欢的一道菜肴。但是经过腌制后的酸菜，维生素C已丧失殆尽；此外，酸菜中还含有较多的草酸和钙，由于酸度高食用后易被肠道吸收，在经肾脏排泄时极易在泌尿系统形成结石；而腌制后的食物，大多含有较多的亚硝酸盐，与人体中胺类物质生成亚硝胺，是一种容易致癌的物质。
        <br> 解物：猕猴桃。科学发现，多吃富含维生素的食物，可以阻断强致癌物亚硝胺的合成，减少胃癌和食道癌的发生。而猕猴桃被称之维生素C之王，一个猕猴桃基本可以满足人体一天所需的维生素C 。
        <br> 状态3：咸肉、腊肉、香肠等
        <br> 这些东西不仅美味，而且保存时间长，受到人们的喜爱。但是这些含有大量盐的食物，与猪肉中的物质长期腌制在一起，产生亚硝胺，进入人体后又会形成二甲基亚硝胺，是一种很强的致癌性物质。
        <br> 解物：绿茶。经研究发现，引用绿茶可以分解这类物质的危害。每天喝几杯绿茶，可以增进健康。如果实在难挡咸鱼、腊肉的诱惑时，不妨在烹制时将肉蒸煮透，再加点醋，可以使亚硝胺分解，又不失美味。
        <br> 状态4：火锅
        <br> 对于那些原本容易上火，或经常复发口疮的人来说，是雪上加霜。不仅会加重症状，增加复发机会，长期反复，还会诱发食道癌变。其实，火锅除了容易上火外，浓汤中含有较高的"卟啉"物质，经过消化分解后在肝脏代谢生成尿酸，还可能引痛风病。
        <br> 解物：柚子。如果在吃了油腻又麻辣的火锅后，吃个柚子，就能有助于滋阴去火，健脾消食。
        <br> 状态5：烤羊肉串
        <br> 街上的虽然香味阵阵，非常诱人，姑且不论其肉质安全如何，就是这些肉经过明火碳烤后所含的苯比芘，进入人体后，就足以对胃造成癌变的威胁。
        <br> 解物：烤白薯。如果你在有一定卫生检测标准的店铺里食用烤肉制品后，再吃上一个烤白薯，白薯中含有的大量纤维素，可以将烤肉中的有害物质包裹起来排出体外，并能阻止烤肉中大量油脂被人体吸收。
        <br> 状态6：皮蛋
        <br> 不少人经常喜欢用皮蛋煮粥喝。殊不知，皮蛋在制作中，多少都会含有一定量的铅。如果摄入过多的铅，将导致智力下降，损害神经系统的发育，引起听力异常，学习能力降低等现象，尤其是儿童机体排铅能力要远远低于成人，因此危害更大。
        <br> 解物：豆腐。"皮蛋豆腐"也是一道家常菜，吃法颇为科学。研究显示。豆制品中的纤维素可以抑制食物中铅在胃肠道的吸收；而其中的钙离子也可以抗铅，帮助排解人体摄入的铅，有助于降低人体血液中金属铅的浓度。
