      <p class="f-cont">
        意识治疗一切。
        <br> ——费兹士·波尔士
        <br> 上个世纪最不寻常的一项科学发现就是，对宇宙中任何现象的观察——观察鸟如何飞行，或地球如何转动——都会对该现象产生直接影响。所以，如果我们能够靠意识扭转天体的运行轨道，那么说维他命A——即意识——能够对人类的身体产生深远的影响就不是什么令人吃惊的事了。 就像你在照镜子时所看到的，表情突然兴奋起来，体内的能量也随之振奋起来，这就是意识在刺激新陈代谢的化学物质；你是否有过这样的经历，当你身处大自然当中，被美丽的风景环绕，顿时觉得神清气爽，精神放松？这同样是意识作用于身体生理系统的结果；或者你是否已经注意到，人在受到关注时行为和表达都会更具有活力，这是因为有其他意识作用于你的生物化学成分。
        <br> 意识是存在的。我们能够感觉到它。我们身体同样有能力感觉到此时此刻生命在做些什么，并且当我们把意识带进饮食过程中时，它将会是一种非常不错的新陈代谢动力。
        <br> 消化从思想开始
        <br> 意识对营养的吸收和消化及对热量的燃烧所起到的推动力，这正好验证了科学家们所说的“头期消化反应”（CPDR）。头期消化反应纯粹是对一顿饭的色香味及满意程度的一个特别定义。有趣的是，研究人员发现，我们的消化工作的30%—40%应归功于头期消化反应——即我们对所吃食物投入的全部意识。
        <br> 你能回想起这样的情景吗，你看到自己最喜欢的食品时开始流口水或者肚子咕咕叫？这就是头期消化反应。当我们的口腔和鼻腔闻到、品尝、咀嚼和注意到食物时，大脑就已经开始消化了。大脑对食物的反应刺激了口水、胃酸、酶和肠神经肽的产生，同时产生各种胰腺酶：胰岛素、胰凝乳蛋白酶、胰腺淀粉酶、脂肪酶等。不仅如此，血液也开始流入消化器官内，胃和肠有节奏地联合起来，电解酶通过消化道聚集，为消化即将到来的食物做好准备。
        <br> 如果我们在吃饭时没有投入足够的注意力，流入消化系统的血液就会减少，也就意味着氧化作用所需的氧气和新陈代谢动力不足。这样，肠内酶的分泌量减少，导致消化系统紊乱，肠胃失调，免疫力下降，并且易疲劳。
        <br> 教你走出家庭日常饮食误区
        <br> 最近专家研究表明，对于很多家庭而言，饮食方面存在着许多误区。他说，对于喜欢吃炒菜的市民，应该注意对菜的烹调方法；对于喜欢煲汤的人，煲汤时间也很有讲究。
        <br> 有关专家说，有些人为了减肥不食脂肪，而偏爱吃对于本身含脂肪及热量相当低的蔬菜，这其实是一个误区。研究表明：本身含脂肪及热量相当低的蔬菜，在经过炒、煎等烹调后，吸入的油脂量远远高于原来本身已有较多脂肪的肉类。这主要是由于蔬菜的细胞与细胞间的空隙充满了空气，而鱼和肉细胞与细胞的空隙充满了液体，所以前者较易吸入油脂，一碗炒菜所吸入的油脂比一碗炸鱼或炸排骨所含的油脂更多，所以对于减肥者来说，多吃炒菜不但收不到减肥的效果，反而会增加体重。再有，调味作料多味道好，但胡椒、桂皮、丁香、小茴香等天然调味品具有一定的诱癌性和毒性，在饮食中过多使用，轻者有口干、咽喉痛、精神不振、失眠等感觉，重者会诱发高血压、胃肠炎等多种疾病，甚至有损伤细胞形成癌症的可能。因此，日常饮食中要尽量少用上述调味料为宜。
        <br> “汤是每天必备的，对于许多市民来说，认为煲汤时间越长营养越多，这也是一个误区。”。“以炖骨头汤为例，很多人认为熬得时间越长，骨头里的钙质才能全跑出来，烧煮时间少则一二个小时，多则三五个小时，有的甚至烧一天，殊不知，这种无谓的烧煮是毫无科学道理的。” 专家还指出，食物过多地在高温下烧煮，时间越长，损失的营养越多。人们平常爱喝的骨头汤，无论多高温度，花多长时间，都不能将骨内的钙质完全溶出，这是因为动物骨骼中所含的钙质不易被分解，久煮反而会破坏骨头中的蛋白质。正确的方法是将骨头砸开，然后放入一定冷水，在炉火中慢慢加温，烧开后加一点醋，使骨头中的磷、钙溶解到汤内，整个加热过程以一小时左右为宜，这样煲出来的汤既有营养又鲜美可口。 为了方便大家的记忆，专家总结如下，希望您能尽快走出家庭日常饮食误区：
        <br> 误区一：吃素不吃荤
        <br> 现在吃素食的人越来越多，有一部分人是因为害怕荤食会引起肥胖、冠心病等。但如果绝对食素，并非有利于健康。食素者蛋白质往往摄入不足，会发生抵抗力下降、浑身无力、水肿等情况。另外由于食素者食物单调，机体中掌管食物消化的酶系统功能逐渐遭到破坏，最后会导致物质交换失调。植物性食品中含有丰富的维生素、无机盐和有机酸，而缺少造血的微量元素钴、锰、铁和铜等。此外，植物性食品脂肪含量少，但人体每天需要50克左右的脂肪，要想满足这种需要，就要大约吃5千克的植物性食品，而且，植物蛋白很难代替动物蛋白。故从抗衰益寿的角度看，绝对素食不可取，只有荤素搭配、营养全面、平衡膳食，才是通往健康长寿之路。
        <br> 误区二：煲汤时间越长营养越多
        <br> 人们认为煲汤时间越长，汤里的营养越多。以炖骨头汤为例，很多人认为熬得时间越长，骨头里的钙质才能全跑出来，烧煮时间少则一二个小时，多则三五个小时，有的甚至烧一天。殊不知，这种无谓的烧煮是毫无科学道理的。营养学家指出，食物过多地在高温下烧煮，时间越长，损失的营养越多。人们平常爱喝的骨头汤，无论多高温度，花多长时间，都不能将骨内的钙质完全溶出，这是因为动物骨骼中所含的钙质不易被分解，久煮反而会破坏骨头中的蛋白质。正确的方法是将骨头砸开，然后放入一定冷水，在炉火中慢慢加温，烧开后加一点醋，使骨头中的磷、钙溶解到汤内，整个加热过程以一小时左右为宜。这样煲出来的汤既有营养又鲜美可口。
        <br> 误区三：久存蔬菜
        <br> 许多人为了节约时间，一次从菜市场买回许多蔬菜后，放在冰箱中慢慢食用，这样做是很不好的。因为蔬菜，尤其是叶菜中含有硝酸盐，在储藏一段时间后，由于酶和细菌的作用，硝酸盐被还原成亚硝酸盐，而亚硝酸盐在人体内与蛋白质类物质结合，可生成致癌性的亚硝酸胺类物质。长期储存蔬菜不仅产生有害物质，而且造成营养素的损失。实验证明，在30℃的屋子里储藏24小时，绿叶蔬菜中的维生素C几乎全部损失。
        <br> 误区四：偏爱吃炒菜
        <br> 有些人为了减肥不食脂肪，而偏爱吃炒菜。但最近国外专家研究显示：本身含脂肪及热量相当低的蔬菜，在经过炒、煎等烹调后，吸入的油脂量远远高于原来本身已有较多脂肪的肉类。专家研究发现，含水量丰富的食物(如各类蔬菜)，经过烹制之后，吸入的脂肪量最多。这主要是由于蔬菜的细胞与细胞间的空隙充满了空气，而鱼和肉细胞与细胞的空隙充满了液体，所以前者较易吸入油脂，一碗炒菜所吸入的油脂比一碗炸鱼或炸排骨所含的油脂更多，所以对于减肥者来说，多吃炒菜不但收不到减肥的效果，反而会增加体重。
        <br> 误区五：调味作料多味道好
        <br> 医学研究表明，胡椒、桂皮、丁香、小茴香等天然调味品具有一定的诱癌性和毒性，在饮食中过多使用，轻者有口干、咽喉痛、精神不振、失眠等感觉，重者会诱发高血压、胃肠炎等多种疾病，甚至有损伤细胞形成癌症的可能。因此，日常饮食中要尽量少用上述调味料为宜。
