        <br> 一般来说，猪肉的瘦肉中的脂肪含量是各种肉中最高的，达25%～30%，而兔肉最低，仅为0.5%～2%。鸡肉（不带皮）的脂肪含量也比较低。牛肉的脂肪含量一般在10%以下，但如果是肥牛，即便是里脊部位也布满细细的脂肪点，脂肪含量甚至超过猪肉。
        <br> 植物油利长寿
        <br> 人群调查和实验证明，动物脂肪摄入量高的人，心血管疾病发病率较高，植物油摄入量高的人，心血管疾病发病率确实低一些，但奇怪的是，两类人的寿命并没有大的差别。经调查，原因是植物油摄入高的人癌症发病率比较高。如果多吃植物油，最好能够补充摄入维生素e等抗氧化物质。
        <br> 肉类蛋白质优
        <br> 动物性食品中的蛋白质确实质量高，但是廉价的豆类和含油种子如花生、葵花子等也含有丰富的蛋白质。
        <br> 氨基酸多的
        <br> 氨基酸本身并没有什么神秘之处，它只是蛋白质的组成单元。食品中含有蛋白质，也自然含有氨基酸。廉价的玉米和土豆中照样含有多种氨基酸。健康人既然具有消化蛋白质的能力，就完全可以从普通食物中获得氨基酸，也就没有必要喝什么昂贵的氨基酸营养液。
