        <span>注意事项</span>
      </p>
      <p class="f-cont">
        1）测量体温前、后，应清点体温计总数。手甩体温计时要用腕部力量，勿触及他物，以防撞碎。切忌把体温计放入热水中清洗或放在沸水中煮，以防爆裂。
        <br> 2）根据病人病情选择合适的测量体温的方法：①凡婴幼儿、精神异常、昏迷、口鼻腔手术以及呼吸困难、不能合作的病人，不宜测口腔温度；②凡消瘦不能夹紧体温计、腋下出汗较多者，以及腋下有炎症、创伤或手术的病人不宜使用腋下测温法；③凡直肠或肛门手术、腹泻，以及心肌梗死的病人不宜使用直肠测温法。
        <br> 3）病人进食、饮水，或进行蒸汽吸入、面颊冷热敷等，须隔30分钟后测口腔温度；腋窝局部冷热敷应隔30分钟再测量腋温；灌肠、坐浴后须隔30分钟，方可经直肠测温。
