        <br> 5）凡给婴幼儿、昏迷、危重病人及精神异常者测体温时，应有专人看护，以免发生意外。
        <br> 6）如发现体温与病情不相符合，应守在病人身旁重新测量，必要时可同时测口温和肛温作对照。
