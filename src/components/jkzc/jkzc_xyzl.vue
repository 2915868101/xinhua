        <span>血液形成</span>
      </p>
      <p class="f-cont">
        每个人体内的血液，都是自己体内产生的，不是由母体血液流入胎儿血管先天带来的。胎儿早期发育时，在其胚胎体内部，就逐步产生了自己的造血中心。当胚胎发育到第三周时，卵黄囊壁上的血岛就是第一个造血的中心，这个中心的造血期到第九周为止。胚胎发育到第六周时，肝脏开始造血，9-24周的胎儿，肝脏是主要的造血场所。肝脏造血以红细胞为主，同时也少量生成粒细胞和巨核细胞，但不生成淋巴细胞。在这期间，脾、肾、胸腺和淋巴结等处也参与造血。脾脏产生于胚胎第三个月，开始以生成红细胞为优势，以后也生成一定数量的粒细胞、淋巴细胞和单核细胞。胸腺为人体周围淋巴组织提供前T细胞，这就是为身体生成具有免疫功能的T淋巴细胞的来源。淋巴结参与早期生成红细胞，但到胚胎发育进入第四个月后，就成为终身造淋巴细胞和浆细胞的器官。当胚胎发育进入第四个月以后，骨髓开始造血，到第五个月以后，肝、脾造血功能逐步减退，骨髓造血功能迅速增加，成为红细胞、粒细胞和巨核细胞的主要生成器官，同时也产生淋巴细胞和单核细胞。胎儿出生以后，肝脏造血功能很快停止，但脾脏仍是终身造淋巴细胞的器官，而骨髓则是人体最重要的造血器官。在正常情况下它不仅生成红细胞、粒细胞和血小板，同时也生成淋巴细胞和单核细胞。骨髓每秒钟可造出1700万个血细胞。人体血液中所有不同的血细胞，都是来自于肝脏、骨髓和胸腺里的始祖细胞——多能干细胞及由此移行的定向干细胞。这就是人体血液产生的基本道理。
