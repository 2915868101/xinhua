      <p class="f-cont">
        制造及降解
        <br> 血细胞在骨髓产生，过程称为“血细胞生成”。蛋白质构成部分，包括凝血因子，主要由肝脏产生，而激素由内分泌腺产生，至于水状成份则由丘脑下部调节肾脏去维持，肠道也有份间接参与。
        <br> 血细胞在脾脏及肝枯否细胞降解，肝也有移除一些蛋白质、脂肪及氨基酸。肾脏把身体的废物带进尿液。正常的红血球在血浆中约有120天寿命。
        <br>输送氧气
        <br> 一个在正常气压环境中呼吸的健康人类，他的动脉血液中的氧约有98.5%与血红素产生化学结合，只有1.5%是溶于其它血液成份中。血红素也是哺乳类及许多其它物种的主要氧输送者。除了肺动脉、脐动脉及两者的对应静脉外，带氧血液从心脏经过动脉、小动脉及毛细血管到达身体各处，然后脱氧血液经小静脉及静脉流回心脏。
