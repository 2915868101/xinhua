      <p class="f-cont">
        一种较安全、易行、应用广泛的血液净化方法之一血液透析。透析是指溶质通过半透膜，从高浓度溶液向低浓度方向运动。血液透析包括溶质的移动和水的移动，即血液和透析液在透析器(人工肾)内借半透膜接触和浓度梯度进行物质交换，使血液中的代谢废物和过多的电解质向透析液移动，透析液中的钙离子、碱基等向血液中移动。
        <br> 把白蛋白和尿素的混合液放入透析器中，管外用水浸泡，这时透析器管内的尿素就会通过人工肾膜孔移向管外的水中，白蛋白因分子较大，不能通过膜孔。这种小分子物质能通过而大分子物质不能通过半透膜的物质移动现象称为弥散。临床上用弥散现象来分离纯化血液使之达到净化目的的方法即为血液透析的基本原理。血液透析所使用的半透膜厚度为10－20微米，膜上的孔径平均为3纳米，所以只允许分子量为1.5万以下的小分子和部分中分子物质通过，而分子量大于3.5万的大分子物质不能通过。蛋白质、致热源、病毒、细菌以及血细胞等都是不可透出的；尿的成分中大部分是水，要想用人工肾替代肾脏就必须从血液中排出大量的水分，人工肾只能利用渗透压和超滤压来达到清除过多的水分之目的。现在所使用的人工肾即血液透析装置都具备上述这些功能，从而对血液的质和量进行调节，使之近于生理状态 。
