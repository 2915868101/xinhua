      <p class="f-tit">
        <span>造血物质</span>
      </p>
      <p class="f-cont">
        血液本身是一种高营养的物质，因此造血也必须有很广的营养原料。造血所需的主要原料有蛋白质、碳水化合物、铁、铜、叶酸、维生素C、维生素B12以及多种微量元素和激素等物质。蛋白质是生命存在的重要基础，血液中最主要的物质就是各种蛋白质，尤其是红细胞。如果蛋白质供量不足，造血器官就无法生产出优质的血液，就会出现贫血而直接影响身体的健康。成人每天需要蛋白质约75克。蛋白质原料的食品，主要有鱼、肉、蛋类、奶类、豆类以及玉米、马铃薯、花生等。铁是血红蛋白中的主要成分。血红蛋白携带氧气和二氧化碳的功能是通过血红蛋白中的铁的结合作用来完成的。如果铁量不够，就会出现缺铁性贫血。成年人每天需要铁量为12毫克。含铁较多的食品主要有海带、黑木耳、菠菜，其次是动物的肝脏、血、肉、蛋黄，豆类、稻米中也有较多的铁含量。叶酸对于蛋白质的生物合成有重要影响。如果供量不足，就会出现巨红细胞贫血，这也叫叶酸缺乏病。成人每天需要量为200微克。含这种物质较多的食品有肉类、肝脏、豆类、蛋类、水果及绿叶蔬菜等。维生素B12、维生素C及其他多种微量元素和激素等，虽然在红细胞内含量不多，但对红细胞的成熟和铁的吸收等造血过程，都是不可缺少的物质。不同的食品，所含营养物质的种类和数量不尽相同，因此在膳食时应力求广泛多样。这样做可使机体在摄取营养过程中可以达到充分的互补。为了更好地摄取造血需要的营养原料，既不可暴饮暴食，又不要偏食。造血的物质指的是制造红细胞的物质，主要有蛋白质、铁、维生素B12、B6、叶酸等。蛋白质来源自肉类及豆类；铁来源自蛋黄、牛肉、肝、肾、豆类、叶绿素、海带及波菜等，另一个重要来源为铁锅所游离出来的少量铁；B12及B6来源较多的是肉、肝、肾、蔬菜等；叶酸的主要来源为蔬菜、酵母及动物内肝。在诸多造血物质中，铁是主要的物质，也是非常容易流失的物
        <br>质，人体内铁平均量约为3-4.5g，男性全身铁量约为500mg/kg，女性为35mg/kg，铁在十二指肠及空肠上段被吸收进入血液，并与其他物质结合组成10类以上有生理功能的化合物，其中最重要的是铁与卟啉环结合成血红素，血红素是红蛋白的主要物质，而血红蛋白又是红细胞的主要物质。据测定，红细胞中的96%是血红蛋白。维生素B12和叶酸是合成DNA的主要辅酶，这两种物质如果不足，将会引起幼红细胞的发育障碍。
