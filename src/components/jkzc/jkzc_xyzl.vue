      <p class="f-tit">
        <span>血细胞类别</span>
      </p>
      <p class="f-cont">
        血细胞分为三类：红细胞、白细胞、血小板。
        <br> 红细胞
        <br> 红细胞（erythrocyte，red blood cell）直径7～8.5μm，呈双凹圆盘状，中央较薄（1.0μm），周缘较厚（2.0μm），故在血涂片标本中呈中央染色较浅、周缘较深（见彩图）。在扫描电镜下，可清楚地显示红细胞这种形态特点。红细胞的这种形态使它具有较大的表面积（约140μm2），从而能最大限度地适应其功能――携O2和部分CO2。新鲜单个红细胞为黄绿色，大量红细胞使血液呈猩红色，而且多个红细胞常叠连一起呈串钱状，称红细胞缗线。
        <br> 红细胞有一定的弹性和可塑性，细胞通过毛细血管时可改变形状。红细胞正常形态的保持需ATP供给能量，由于红细胞缺乏线粒体，ATP只由无氧糖酵解产生；一旦缺乏ATP供能，则导致细胞膜结构改变，细胞的形态也随之由圆盘状变为棘球状。这种形态改变一般是可逆的。可随着ATP的供能状态的改善而恢复。
        <br> 成熟红细胞无细胞核，也无细胞器，胞质内充满血红蛋白（hemoglobin,Hb）。血红蛋白是含铁的蛋白质，约占红细胞重量的33%。它具有结合与运输O2和CO2的功能，当血液流经肺时，肺内的O2分压高（102mmHg），CO2分压低(40mmHg)，血红蛋白(氧分压40mmHg,二氧化碳分压46mmHg)即放出CO2而与O2结合；当血液流经其它器官的组织时，由于该处的CO2分压高（46mmHg）而O2分压低（40mmHg），于是红细胞即放出O2并结合CO2。由于血红蛋白具有这种性质，所以红细胞能供给全身组织和细胞所需的O2，带走所产生的部分CO2。
        <br> 正常成人每微升血液中红细胞数的平均值，男性约400万～500万个，女性约350万～450万个。血液中血红蛋白含量，男性约120～160g/L，女性约110～140g/L，新生儿为170—200g/L。全身所有红细胞表面积总计，相当于人体表面积的2000倍。红细胞的数目及血红蛋白的含量可有生理性改变，如婴儿高于成人，运动时多于安静状态，高原地区居民大都高于平原地区居民，红细胞的形态和数目的改变、以及血红蛋白的质和量的改变超出正常范围，则表现为病理现象。一般说，红细胞数少于300万/μ1为贫血，血红蛋白低于100g/L则为缺铁性贫血。此时常伴有红细胞的直径及形态的改变，如大红细胞贫血的红细胞平均直径>9μm，小红细胞贫血的红细胞平均直径&lt;6μm。缺铁性贫血的红细胞，由于血红蛋白的含量明显降低，以致中央淡染区明显扩大。
        <br> 红细胞的渗透压与血浆相等，使出入红细胞的水分维持平衡。当血浆渗透压降低时，过量水分进入细胞，细胞膨胀成球形，甚至破裂，血红蛋白逸出，称为溶血（hemolysis）；溶血后残留的红细胞膜囊称为血影（ghost）。反之，若血浆的渗透压升高，可使红细胞内的水分析出过多，致使红细胞皱缩。凡能损害红细胞的因素，如脂溶剂、蛇毒、溶血性细菌等均能引起溶血。
        <br> 红细胞的细胞膜，除具有一般细胞膜的共性外，还有其特殊性，例如红细胞膜上有ABO血型抗原。
        <br> 外周血中除大量成熟红细胞以外，还有少量未完全成熟的红细胞，称为网织红细胞（reticulocyte）在成人约为红细胞总数的0.5%～1.5%，新生儿较多，可达3%～6%。网织红细胞的直径略大于成熟红细胞，在常规染色的血涂片中不能与成熟红细胞区分。用煌焦蓝作体外活体染色，可见网织红细胞的胞质内有染成蓝色的细网或颗粒，它是细胞内残留的核糖体。核糖体的存在，表明网织红细胞仍有一些合成血红蛋白的功能。红细胞完全成熟时，核糖体消失，血红蛋白的含量即不再增加。贫血病人如果造血功能良好，其血液中网织红细胞的百分比值增高。因此，网织红细胞的计数有一定临床意义，它是贫血等某些血液病的诊断、疗效判断和估计预指标之一。
        <br> 红细胞的平均寿命约120天。衰老的红细胞虽无形态上的特殊樗，但其机能活动和理化性质都有变化，如酶活性降低，血红蛋白变性，细胞膜脆性增大，以及表面电荷改变等，因而细胞与氧结合的能力降低且容易破碎。衰老的红细胞多在脾、骨髓和肝等处被巨噬细胞吞噬，同时由红骨髓生成和释放同等数量红细胞进入外周血液，维持红细胞数的相对恒定。成熟的红细胞没有细胞核。
        <br> 白细胞
        <br> 白细胞（leukocyte,white blood cell）为无色有核的球形细胞，体积比红细胞大，能作变形运动，具有防御和免疫功能。成人白细胞的正常值为4000～10000个/μ1。男女无明显差别。婴幼儿稍高于成人。血液中白细胞的数值可受各种生理因素的影响，如劳动、运动、饮食及妇女月经期，均略有增多。在疾病状态下，白细胞总数及各种白细胞的百分比值皆可发生改变。
        <br> 光镜下，根据白细胞胞质有无特殊颗粒，可将其分为有粒白细胞和无粒白细胞两类。有粒白细胞又根据颗粒的嗜色性，分为中性粒细胞、嗜酸性粒细胞和嗜碱性粒细胞。无粒白细胞有单核细胞和淋巴细胞两种。
        <br> 中性粒细胞：
        <br> 中性粒细胞（neutrophilic granulocyte,neutrophil）占白细胞总数的50%－70%，是白细胞中数量最多的一种。细胞呈球形，直径10－12μm，核染色质呈团块状。核的形态多样，有的呈腊肠状，称杆状核；有的呈分叶状，叶间有细丝相连，称分叶核。细胞核一般为2～5叶，正常人以2～3叶者居多。在某些疾病情况下，核1～2叶的细胞百分率增多，称为核左移；核4～5叶的细胞增多，称为核右移。一般说核分叶越多，表明细胞越近衰老，但这不是绝对的，在有些疾病情况下，新生的中性粒细胞也可出现细胞核为5叶或更多叶的。杆状核粒细胞则较幼稚，约占粒细胞总数的5%～10%，在机体受细菌严重感染时，其比例显著增高。
        <br> 中性粒细胞的胞质染成粉红色，含有许多细小的淡紫色及淡红色颗粒，颗粒可分为嗜天青颗粒和特殊颗粒两种。嗜天青颗粒较少，呈紫色，约占颗粒总数的20%，光镜下着色略深，体积较大；电镜下呈圆形或椭圆形，直径0.6～0.7μm，电子密度较高，它是一种溶酶体，含有酸性磷酸酶和过氧化物酶等，能消化分解吞噬的异物。特殊颗粒数量多，淡红色，约占颗粒总数的80%，颗粒较小，直径0.3～0.4μm，呈哑铃形或椭圆形，内含碱性磷酸酶、吞噬素、溶菌酶等。吞噬素具有杀菌作用，溶菌酶能溶解细菌表面的糖蛋白。
        <br> 中性粒细胞具有活跃的变形运动和吞噬功能。当机体某一部位受到细菌侵犯时，中性粒细胞对细菌产物及受感染组织释放的某些化学物质具有趋化性，能以变形运动穿出毛细血管，聚集到细菌侵犯部位，大量吞噬细菌，形成吞噬小体。吞噬小体先后与特殊颗粒及溶酶体融合，细菌即被各种水解酶、氧化酶、溶菌酶及其它具有杀菌作用的蛋白质、多肽等成分杀死并分解消化。由此可见，中性粒细胞在体内起着重要的防御作用。中性粒细胞吞噬细胞后，自身也常坏死，成为脓细胞。中性粒细胞在血液中停留约6～7小时，在组织中存活约1～3天。
        <br> 嗜酸性粒细胞：
        <br> 嗜酸性粒细胞（eosinophilic granulocyte,eosinophil）占白细胞总数的0.5%－3%。细胞呈球形，直径10～15μm，核常为2叶，胞质内充满粗大（直径0.5～1.0μm）、均匀、略带折光性的嗜酸性颗粒，染成桔红色。电镜下，颗粒多呈椭圆形，有膜包被，内含颗粒状基质和方形或长方形晶体。颗粒含有酸性磷酸酶、芳基硫酸酯酶、过氧化物酶和组胺酶等，因此它也是一种溶酶体。嗜酸性粒细胞也能作变形运动，并具有趋化性。它能吞噬抗原抗体复合物，释放组胺酶灭活组胺，从而减弱过敏反应。嗜酸性粒细胞还能借助抗体与某些寄生虫表面结合，释放颗粒内物质，杀灭寄生虫。故而嗜酸性粒细胞具有抗过敏和抗寄生虫作用。在过敏性疾病或寄生虫病时，血液中嗜酸性粒细胞增多。它在血液中一般仅停留数小时，在组织中可存活8～12天。
        <br> 嗜碱性粒细胞：
        <br> 嗜碱性粒细胞（basoophilic granulocyte,basophil）数量最少，占白细胞总数的0～15。细胞呈球形，直径10－12μm。胞核分叶或呈S形或不规则形，着色较浅。胞质内含有嗜碱性颗粒，大小不等，分布不均，染成蓝紫色，可覆盖在核上。颗粒具有异染性，甲苯胺蓝染色呈紫红色。电镜下，嗜碱性颗粒内充满细小微粒，呈均匀状或螺纹状分布。颗粒内含有肝素和组胺，可被快速释放；而白三烯则存在于细胞基质内，它的释放较前者缓慢。肝素具有抗凝血作用，组胺和白三烯参与过敏反应。嗜碱性粒细胞在组织中可存活12－15天。 嗜碱性粒细胞与肥大细胞，在分布、胞核的形态，以及颗粒的大小与结构上，均有所不同。但两种细胞都含有肝素、组胺和白三烯等成分，故嗜碱性粒细胞的功能与肥大细胞相似，但两者的关系尚待研究。
        <br> 单核细胞：
        <br> 单核细胞（monocyte）占白细胞总数的3%～8%。它是白细胞中体积最大的细胞。直径14～20μm，呈圆形或椭圆形。胞核形态多样，呈卵圆形、肾形、马蹄形或不规则形等。核常偏位，染色质颗粒细而松散，故着色较浅。胞质较多，呈弱嗜碱性，含有许多细小的嗜天青颗粒，使胞质染成深浅不匀的灰蓝色。颗粒内含有过氧化物酶、酸性磷酸酶、非特异性酯酶和溶菌酶，这些酶不仅与单核细胞的功能有关，而且可作为与淋巴细胞的鉴别点。电镜下，细胞表面有皱褶和微绒毛，胞质内有许多吞噬泡、线粒体和粗面内质网，颗粒具溶酶体样结构。 单核细胞具有活跃的变形运动、明显的趋化性和一定的吞噬功能。单核细胞是巨噬细胞的前身，它在血流中停留1－5天后，穿出血管进入组织和体腔，分化为巨噬细胞。单核细胞和巨噬细胞都能消灭侵入机体的细菌，吞噬异物颗粒，消除体内衰老损伤的细胞，并参与免疫，但其功能不及巨噬细胞强。
        <br> 淋巴细胞：
        <br> 淋巴细胞（lymphocyte）占白细胞总数的20%～30%，圆形或椭圆形，大小不等。直径6～8μm的为小淋巴细胞，9～12μm的为中淋巴细胞，13～20μm的为大淋巴细胞。小淋巴细胞数量最多，细胞核圆形，一侧常有小凹陷，染色质致密呈块状，着色深，核占细胞的大部，胞质很少，在核周成一窄缘，嗜碱性，染成蔚蓝色，含少量嗜天青颗粒。中淋巴细胞和大淋巴细胞的核椭圆形，染色质较疏松，故着色较浅，胞质较多，胞质内也可见少量嗜天青颗粒。少数大、中淋巴细胞的核呈肾形，胞质内含有较多的大嗜天青颗粒，称为大颗粒淋巴细胞、电镜下，淋巴细胞的胞质内主要是大量的游离核糖体，其他细胞器均不发达。
        <br> 以往曾认为，大、中、小淋巴细胞的分化程度不同，小淋巴细胞为终末细胞。但如今普遍认为，多数小淋巴细胞并非终末细胞。它在抗原刺激下可转变为幼稚的淋巴细胞，进而增殖分化。而且淋巴细胞也并非单一群体，根据它们的发生部位、表面特征、寿命长短和免疫功能的不同，至少可分为T细胞、B细胞、杀伤（K）细胞和自然杀伤（NK）细胞等四类。
        <br> 血液中的T细胞约占淋巴细胞总数的75%，它参与细胞免疫，如排斥异移体移植物、抗肿瘤等，并具有免疫调节功能。B细胞约占血中淋巴细胞总数的10%～15%。B细胞受抗原刺激后增殖分化为浆细胞，产生抗体，参与体液免疫（详见免疫系统）。
        <br> 血小板
        <br> 血小板（platelet）是哺乳动物血液中的有形成分之一。它有质膜，没有细胞核结构，一般呈圆形，体积小于红细胞和白细胞。血小板在长期内被看作是血液中的无功能的细胞碎片。直到1882年意大利医师J．B．比佐泽罗发现它们在血管损伤后的止血过程中起着重要作用，才首次提出血小板的命名。
        <br> 血小板具有特定的形态结构和生化组成，在正常血液中有较恒定的数量（如人的血小板数为每立方毫米10～30万），在止血、伤口愈合、炎症反应、血栓形成及器官移植排斥等生理和病理过程中有重要作用。
        <br> 血小板只存在于哺乳动物血液中。低等脊椎动物圆口纲有纺锤细胞起凝血作用，鱼纲开始有特定的血栓细胞。两栖、爬行和鸟纲动物血液中都有血栓细胞，血栓细胞是有细胞核的梭形成椭圆形细胞，功能与血小板相似。无脊椎动物没有专一的血栓细胞，如软体动物的变形细胞兼有防御和创伤治愈作用。甲壳动物只有一种血细胞，兼有凝血作用。
        <br> 血小板为圆盘形，直径1～4微米到7～8微米不等，且个体差异很大(5～12μm3)。血小板因能运动和变形，故用一般方法观察时表现为多形态。血小板结构复杂，简言之，由外向内为3层结构，即由外膜、单元膜及膜下微丝结构组成的外围为第1层；第2层为凝胶层，电镜下见到与周围平行的微丝及微管构造；第3层为微器官层，有线粒体、致密小体、残核等结构。
        <br> 血细胞形态、数量、比例和血红蛋白含量的测定称为血像。患病时，血像常有显著变化，故检查血像对了解机体状况和诊断疾病十分重要。
