      <p class="f-cont">
        血液是在心脏和血管腔内循环流动的一种组织。成人的血液约占体重的十三分之一，相对密度为1.050～1.060，pH值为7.3～7.4，渗透压为313毫摩每升。由血浆和血细胞组成。血浆内含血浆蛋白（白蛋白、球蛋白、纤维蛋白原）、脂蛋白等各种营养成分以及无机盐、氧、激素、酶、抗体和细胞代谢产物等。血细胞有红血球、白血球和血小板。机体的生理变化和病理变化往往引起血液成分的改变，所以血液成分的检测有重要的临床意义。ABO血型是人类的主要血型分類，可分為A型、B型、AB型及O型。
        <br> 血液的功能包含血细胞功能和血浆功能两部分，有运输、调节人体温度、防御、调节人体渗透压和酸碱平衡四个功能。红细胞主要功能是运进氧气运出二氧化碳，白细胞的主要功能是杀灭细菌，抵御炎症，参与体内免疫发生过程，血小板主要在体内发挥止血功能，血浆功能主要为营养，运输脂类，缓冲，形成渗透压，参与免疫，参与凝血和抗凝血功能。人体内血液的总量称为血量，是血浆量和血细胞的总和，但除红细胞外，其它血细胞数量很少，常忽略不计。每个人体内的血液量，是根据各人的体重来决定的。正常人的血液总量约相当于体重的7%-8%，或相当于每公斤体重70-80ML，其中血浆量为40-50ML。每立方毫米血液中有400-500万个红血球，4000-11000个白血球，15-40万个血小板。另外，同样体重的人，瘦者比肥胖人的血量稍多一点，男人比女人的血量要多一些。安全的血液指的是这样的血液，它不含有任何病毒、寄生虫、药物、酒精、化学物质或其他能给受血者带来损害、危险或疾病的外来物质。献血者必须身体健康，没有也未曾得过任何严重的疾病。受血者不应因受血而受到损害，献血者也不应因献血而招致风险。血液的颜色是有差别的，血液的红色的来自红细胞内的血红蛋白，血红蛋白含氧量多时呈鲜红色（动脉血），含氧量少的呈暗红色（静脉血）。通常献血抽的是静脉血，所以外观看上去呈暗红色。若血含较多的是高铁血红蛋白或其他血红蛋白衍生物，则呈紫黑色。血浆（或血清）因含少量胆红素，看上去呈透明淡黄色；若含乳糜微粒，则呈乳白浑浊；若发生溶血，则呈红色血浆。
