      <p class="f-cont">
        临床医生应严格掌握输血适应症，科学、合理用血。手术前应根据术中估计出血量决定申请备血，出血量在10毫升/公斤体重以下者原则上不输血。积极开展手术前自体储血、术中血液稀释等技术。对估计出血量在1000毫升以上者，争取手术野血液回收，减少或避免输同种异体血，杜绝“营养血”、“安慰血”、“人情血”等不必要的输血。积极实行成分输血，减少不必要的血液成分的补充，避免可能由此引起的不良输血反应。手术中适当采用控制性血压等措施，减少出血。目前公认的是英国医生Blundell首先开创了直接输血法，并第一个把人血输给了人。那是在1817—1818年，英国妇产科医生Blundell由经常看到产妇失血死亡而想到用输血来换救生命。他进行了动物之间的输血取得成功后，开始将健康人的血液输给大出血的产妇，一共治了10例，除2例濒死未能救活外，其余8例中有4例救活。因为当时还不知道血型不同的人输血时，红细胞会遭到大量破坏，所以无法解释为什么输血后有人出现致死性的输血反应，有人却活了下来。尽管如此，1818年12月22日他在伦敦举行的内科学会上所作的输血报告，还是引起了医学界的轰动。他还首创了重力输血器，利用重力来作输血时的推动力，这种输血方法一直沿用了100年左右。
        <br> 输血前，必须做ABO血型的正向和反向定型，以及患者和献血者之间的交叉配合试验（含抗球蛋白试验），输血液成分，按成分血的要求配血。在Rho(D)阴性率较高的地区，还应常规做Rho（D）血型检定。对经产妇或有输血史的患者做不规则抗体检测，交叉配合试验和抗球蛋白试验必须相容才可输血。
        <br> 在第二次世界大战期间，由于战地血源紧缺及输血科学水平低下，以致在同型血源缺乏时不得不采取O型血作为“万能血”，因此，O型血者曾被称为“万能供血者”。这是因为O型血者的红细胞上既无A抗原，也无B抗原，其红细胞可以被A、B、AB型血者接受。当少量输入O型全血时被受血者的大量血液稀释，也就不会大量破坏受血者的红细胞了。但当A、B、AB三种血型者接受较多O型血全血时，O型血血清中存在的抗A抗体、抗B抗体，就会与以上三种血型者的红细胞结合，使受血者发生溶血性输血反应。所以，现在凡是输全血都是使用同型血。可以说，“万能供者”的观念早已过时。但是在进行红细胞成分输血时，由于O型血中的红细胞没有A抗原、B抗原，因此将O型血的红细胞输给不同血型的受血者则是可行的 。
