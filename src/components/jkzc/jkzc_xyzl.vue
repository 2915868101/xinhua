      <p class="f-tit">
        <span>生成原理</span>
      </p>
      <p class="f-cont">
        血液的生成很有趣，就像田径场上的接力跑，参与者有胚胎的卵黄囊、肝、脾、肾、淋巴结、骨髓等。造血始于人胚的第3周，此阶段还没有什么器官形成，一个叫卵黄囊的胚胎组织担起造血的第一责任。人胚第6周，人体器官形成，肝脏接着造血。人胚第3个月，脾是主要的造血器官。人胚第4个月后，骨髓开始造血，这是人体最重要的造血组织。出生后，肝、脾造血停止，骨髓负起造血的全部责任。血细胞包括红细胞、白细胞、血小板等，它们各司其职，但都来自同一种细胞——多功能干细胞。由这种细胞增殖、分化和成熟，才变为在血管里流动的各种终末血细胞。
        <br>
      </p>
      <p class="f-tit">
        <span>理化特性</span>
      </p>
      <p class="f-cont">
        血液的温度为37摄氏度，比重为1.050—1.060，红细胞的比重为1.090，血浆的比重为1.025—1.030。血液也是有粘稠度的，即血液在血管内流动的粘滞力，主要取决于红细胞的数量和血浆蛋白的浓度。
        <br> 全血的相对粘稠度为纯水的4—5倍；血浆为1.6—2.4倍；血清粘稠度为1.5倍。血液的PH值为7.5—45，静脉血因含较多的二氧化碳，PH较低，接近7.35，而动脉血则接近7.45，常人血浆在37摄氏度时渗透压为7.6大气压。
        <br> 血液的颜色是有差别的，血液的红色的来自红细胞内的血红蛋白，血红蛋白含氧量多时呈鲜红色（动脉血），含氧量少的呈暗红色（静脉血）。通常献血抽的是静脉血，所以外观看上去呈暗红色。若血含较多的是高铁血红蛋白或其他血红蛋白衍生物，则呈紫黑色。血浆（或血清）因含少量胆红素，看上去呈透明淡黄色；若含乳糜微粒，则呈乳白浑浊；若发生溶血，则呈红色血浆。
        <br>
      </p>
      <p class="f-tit">
        <span>血小板</span>
      </p>
      <p class="f-cont">
        说红细胞小得很，其实血小板比它更小。它的身体只有红细胞的八分之一。在血球家族里，它称得上是一位“小个子”。血小板的数量也不算多，正常人体每立方毫米的血中约有10万～30万个。其实，血小板不像一块“板”，它们多数是两面连起的圆球体，还有些是不规则的“碎片”。
        <br> 1941年，有个英国人首先发现了它，但不知道它有什么用。过了整整14年，它的作用才被人陆续认识。你一定知道，拧开自来水龙头后，水就流出来了，不关住龙头，水就始终在流。假如你不小心划破了皮肤，出了血，可过了几分钟，鲜血就不再从小伤口里往外冒了。那么是谁拧住了血管上的“龙头”呢？告诉你，拧住“血龙头”的主要功臣正是血小板。血小板平常是排列在血管壁旁的，血管一旦有了损伤，它们立即来到“出事地点”，相互粘集在一起，而且越聚越多，粘成一团，加上其它因素，于是伤口被堵住了。医学家认为，身体里几乎每天都有上百次的微细血管破裂，幸亏血小板时时奋勇地堵险抢修，才免生意外。这些体内工程兵实在功勋卓著。当然，假如血液中血小板数量太少，止血功能就会大受影响，说不定还会危及生命。台湾的医学家在1996年发现，血小板中的生长因子还可以帮助伤口的愈合，烧伤病人在植皮前，抽些血液并从中分离出血小板生长因子，将这些因子覆盖在预植皮的伤口上，就可使伤口的生长速度加快百分之二十到百分之三十，且愈合情况更好。但是，“工程兵”过多也不行。有种病叫“血小板过多症”，就是每立方毫米里有血小板多达几百万个，因为它们常会聚在一块，可能形成血栓，堵塞血管，妨碍血流，以致引起严重后果。
        <br>
      </p>
      <p class="f-tit">
        <span>血液血型</span>
      </p>
      <p class="f-cont">
        血型是人类血液型别的一种标志。人与人之间的血型并如完全相同。通常所说的ABO血型，就是指血液中红细胞所带不同的抗原物质而言的。在红细胞上含有A抗原的，称为A型；含有B抗原的，称为B型；同时含有A和B两种抗原的，称为AB型；既不含A抗原又不含B抗原的称为O型。
        <br> Rh是恒河猴（Rhesus Macacus）外文名称的头两个字母。兰德斯坦纳等科学家在1940年做动物实验时，发现恒河猴和多数人体内的红细胞上存在Rh血型的抗原物质，故而命名的。凡是人体血液红细胞上有Rh抗原（又称D抗原）的，称为Rh阳性，而缺乏Rh抗原的人是Rh阴性。这样就使已发现的红细胞A、B、O及AB四种主要血型的人，又都分别一分为二地被划分为Rh阳性和阴性两种。
        <br> Rh阳性血型在我国汉族及大多数民族人中约占99.7%，个别少数民族约为90%。在国外的一些民族中，Rh阳性血型的人约为85%，其中在欧美白种人钟，Rh阴性血型人约占15%。
        <br> 稀有血型就是一种少见或罕见的血型。这种血型不仅在ABO血型系统中存在，而且在稀有血型系统中也还存在一些更为罕见的血型。在稀有血型系统中，除RH血型系统外，其他各血型人数在总人口中所占比例非常小。
        <br> 血型是由特定的遗传物质决定的，这就象种瓜得瓜、种豆得豆一样，因此血型一经确定就不会自行改变。但是，当人体发生某些疾病（白血病）时，尤其是在疾病的中晚期，由于造血器官功能障碍，使不成熟的红细胞大量进入血流。这些不成熟红细胞膜上的血型抗原物质或减少或消失，因此血型就会发生改变。如果造血器官功能恢复，那么改变后的血型又可展现原型。另外，长期大量输血后的病例，血型也会出现暂时性改变，但是这种改变维持不会太久。
        <br> 随着医学科学的发展，人们对于血型的认识也越来越深刻。由于血液内部的组成成分的不同，各自所具有的抗原物质的性质也不一样，因此，血型存在千差万别。如红细胞已发现有20多种血型系统，不同的血型抗原就有400多种。白细胞上的抗原物质更为复杂，仅本身就有8个系统近20种血型抗原，此外还有红细胞血型抗原和与其他组织细胞共有的抗原，其中与其他组织细胞共有的抗原就已检出148个。这类抗原也称为人类白细胞抗原（简称HLA抗原）；血小板有特异性抗原7个系统，内又有10多种抗原，另外还有20多种血清蛋白、血清酶以及30多种抗原种类，共计在600钟以上。如按这个数字再进行排列组合，那么人类血型就有数十亿钟之多。人类除同暖卵双生子女外，再也找不到两个血型完全相同的人。人的性格是人的一种行为表现，性格属于精神范畴而不属于物质范畴。由于性格不是遗传基因物质，因此不存在遗传性。人的性格只是在人出生以后，受环境等诸多因素潜移默化的影响而逐步形成的。同时它又随人的年龄和环境的改变而有所变化，并非“本性难移”，而是具有可塑性的。因此无论从理论上或是事实上，都说明血型与性格之间不存在任何必然性的联系。父母血型与子女血型有一关系。
