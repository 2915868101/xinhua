        <br> 若你躺在床上思考当日所做的事情或次日应做的事情，那你应该在上床前处理完这些分心的事情。列出清单，以便于你不致感觉必须时时提醒自己该做的事而影响睡眠。
        <br> ⒐别在太饱或太饿时上床
        <br> 晚上的一顿大餐迫使你的消化系统超时工作。你虽感困盹，却极可能彻夜辗转难眠。
        <br> 若你在节食，别在饥饿时上床。咕咕叫着的胃像其它身体不适一样会整夜妨碍你安静下来，难以入睡。睡前请吃低卡路里食物，如香蕉或苹果。 ⒑建立“睡眠仪式”
        <br> 在你入睡前，抛开清醒时的一切烦恼。即便是儿童也发现重复一些活动，如每晚的祈祷或读故事书更容易使人入睡。
