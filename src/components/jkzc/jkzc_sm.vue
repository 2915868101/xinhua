        1、冰淇淋
        <br> 冰淇淋含有大量脂肪，吃完就睡，会让身体没有时间去燃烧脂肪。所有的糖会给身体发出错误的能量信息，然后自动储存并变成脂肪。另外，研究发现，睡前吃高糖食物容易做噩梦。
        <br> 2、芹菜
        <br> 芹菜是一种天然利尿剂，会使尿量变多。
        <br>如果睡前摄入太多芹菜，身体会为了撒尿而唤醒你。虽然芹菜能给身体带来丰富的营养，但睡前尽量别吃。
        <br> 3、意大利面
        <br> 意大利面全是碳水化合物，吃完马上入睡，容易变成脂肪，改变你的血糖水平，进而推迟睡眠，或容易在夜里醒来。另外，加在意大利面上的奶酪、奶油或番茄酱还可能加重你的消化系统负担。
        <br> 4、匹萨
        <br> 匹萨中的番茄沙司酸度高，会刺激胃部泛酸。其中的肥肉和奶酪等还会使你胃灼热。
        <br> 5、糖块
        <br> 最新一项研究显示，10个人中有7个睡前会吃糖块等垃圾食品，并导致整晚做噩梦。专家猜测因高糖水平造成更多脑电波，导致做噩梦。
        <br> 6、麦片
        <br> 麦片中常含有大量精糖和碳水化合物，会让人血糖飙升，因此不适合睡前食用。
        <br> 7、大蒜
        <br> 晚上吃大蒜，除了会让你整晚口臭外，还会造成胃灼热。 如果你的胃功能较差或易泛酸，睡前一定不要吃大蒜等辛辣食物。
        <br> 8、巧克力
        <br> 黑巧克力中的咖啡因能使人兴奋。几乎所有巧克力都含有一定水平的咖啡因。除此之外，其中的可可碱，会使人心跳加快。
        <br> 9、酒精
        <br> 一般人认为，酒精会促进睡眠，这是不准确的。酒精确实可以帮助入睡，但它不会维持长时间、优质的休息。
        <br>酒精实际上还会干扰睡眠的恢复功能，经常用酒精催眠的人会陷入必须依靠喝酒才能入睡的恶性循环。
