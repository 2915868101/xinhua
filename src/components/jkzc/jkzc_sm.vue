        <br> 1、当你睡觉时，或多或少都会有移动身体的情况，因此成人床的最理想宽度应是肩的2倍至3倍，而长度在190公分至210公分，这个范围已可以令你睡得舒服了。床垫应考虑其软硬度、弹性及透气等性质，最重要是能够保护腰椎、平均地承托整个人的体重。床铺硬度宜适中，过硬会使人时常翻身，难以安睡，睡后周身酸痛，太软则不利于脊椎的正常发育。
        <br> 2、不妨挑选一些自己喜欢的床单、被褥及睡衣，最理想是色泽柔和、舒服体贴、透气佳、不易磨损等材料。
        <br> 3、说一下枕头，枕头是最重要的睡眠用品了--享受快乐睡眠，选择适合自己的枕头必不可少。对于枕头，适之宝睡眠专门编辑有《枕头的学问》等内容的介绍，全面介绍了枕头的一些产品和使用知识，中国枕头网对于这些内容都有发布。这里简单介绍一下，枕头呢，往往有高低、软硬、弹性等的基本区分，适之宝根据填充材料不同将枕头分为了软枕、硬枕和中性枕三大类，然顾客方便选择，其中天然的中性枕头是我们的创造，就是不软不硬的枕头。长期的经验和研究表明，枕头作为一个睡眠工具，它的基本作用是"承托"，也就是"承托您宝贵的头部"，因此一个枕头重要的评价标准首先是"承托力"，枕头的承托力的评价又要看每个夜晚，枕头对于人体头部的8小时承托整体表现情况。说到这里不能不提到市场上流行的"纤维棉枕头"，纤维棉枕头有什么好处？有什么坏处，好处多还是坏处多？在枕头记中告诉大家，纤维棉枕头的好处有，这个必须承认，例如化学纤维的弹性，舒适性。但是枕头是作为一个睡眠的工具，它的主要作用是促进睡眠，首先是保证人们健康睡眠。这一点，纤维棉枕头很多都做不到。尤其是蓬松枕头，《枕头记》告诉大家，从研究来看，这种看起来很美观的枕头不是让人们健康睡眠的枕头。
        <br> 睡眠姿势
        <br> 睡觉要保持合理的睡眠姿势？那么你该摆什么ＰＯＳＥ睡觉？《枕头记》记录说，右侧位睡眠有助于安睡。睡觉的时候，尽量避免将双手放在胸前，压迫心肺，阻碍呼吸。仰卧不是最理想的姿势，因为仰卧时舌根部往后坠缩，影响呼吸，容易发出鼾声。伏卧会压迫胸腹，容易导致呼吸困难，都是不正确的睡姿。向左而卧也不是好方法，因为压着左心室活动对血型循环不太好。向右侧卧、身体轻微弯曲据说是最佳的姿势，这样可以让全身肌肉松弛、血液流动增多、呼吸畅通。不过适之宝提醒大家，对一个健康人来说，不必过分计较自己的睡眠姿势，没有必要刻意为之，因为在一夜的睡眠过程中，夜间人体翻转次数较多，人是不可能保持一个睡姿到天明的，绝大多数的人还是在变换着姿势睡觉，这样更有利于解乏和恢复体力。一般人以侧卧为佳。仰卧入睡时四肢肌肉得不到放松。睡熟之后手会不自觉地搭压在胸上，容易引起噩梦；俯卧时胸腹部受压，口鼻易被枕头捂住，还容易"落枕"。侧卧时四肢放松，活动范围不大，不易打鼾。适之宝枕工坊认为，如果有某种疾患，最好听取医生意见。对于患有某种疾病或有特殊情况的人而言，讲究一下睡眠姿势就很有必要。因为很多疾病是由于睡眠姿势不当而诱发或加重的。所以，自行或被动采取保护性睡姿，对预防疾病的发生或减轻疾病的症状都是有益的。
        <br> 以下为《枕头记》收集的枕头和睡眠方法：肺结核：两边肺部都有病的人，最好是仰睡。如果左肺有病，适宜左侧睡；右肺有病，则宜右侧睡。心肌炎、哮喘、心力衰竭：采取半躺半坐的睡姿，可改善肺部的血液循环，减少肺部瘀血，增加氧气的吸入量，有利于症状缓解与休息。中耳炎：脓汁会灌满患侧耳道，为使脓汁引流通畅，可采取患侧卧位，以促使脓液排出。脑血栓：动脉硬化若采取侧卧位睡姿，势必加重血流障碍，宜改为仰卧睡姿。胃病：胃溃疡病人应向右侧卧。心脏病：心脏代偿功能尚好者，可向右侧卧。若已出现心衰，可采用半卧位，以减轻呼吸困难，切忌左侧卧或俯卧。高血压：高血压患者特别是老年高血压患者的睡姿应为半卧位或侧卧位，可使用１５厘米高的长方形宽大枕头，使头和肩部都枕上。睡眠 胆石症：不宜左侧卧。因为胆囊位于上腹部，形如一只小酒瓶。当人体向左侧卧时，胆囊"瓶口"朝下方，"瓶底"朝上方。这样，胆囊结石在重力作用下就容易落入"瓶颈部"而发生嵌顿，引起胆绞痛发作。加上夜间病人入睡后，迷走神经张力增强，胆道平滑肌兴奋性增高，胆囊颈管一旦发生痉挛，可使结石嵌顿更严重，疼痛即刻加重且持久。长时间的胆绞痛会引起急性胆囊炎。这是胆结石病人夜间急诊的主要原因之一。因此，胆囊结石病人应尽可能平卧或向右侧睡。腰背痛：宜侧卧睡，这样可以使肌肉完全松弛，避免肌肉牵拉紧张、刺激或压迫神经，引起或加重腰背痛。
        <br> 睡向与健康
        <br> 我国传统的望到讲究坐北朝南，大门开向正南方，房间终年向阳。这样的建筑布局决定了床铺是南北向的，人们的睡眠方向也自然成为南北方向了。如此建筑、如此朝向，其中包含有不少科学道理。
        <br> 人体内含有占体重2/3的水。水分子由两个氢原子和一个氧原子构成。氢原子分布在一端，带有正电荷，氧原子分布在一端，带有负电荷，所以水分子的两端电性相反，一正一负，化学上称水分子为“极性分子”。
        <br> 地球是一个大磁场，磁力线贯穿南北。人体内的水分子就如一根根小小的指南针，在地球磁力线的作用下不停地摆动。当水分子的两极朝向与地球磁力线方向相同时，水分子就停止摆动趋向稳定；水分子两极朝向与地球磁力线不同时，水分子就要旋转到使其朝向与地球南北磁力线方向相同为止。如果人睡眠的方向是南北方向，那么水分子朝向、人体睡眠的方向和地球南北磁力线方向三者一致，这时人最容易入睡，睡眠质量也最高。
        <br> 人体的血液循环系统中，主动脉和大动脉最为重要，其走向与人体的头脚方向一致。人体处于南北朝向时，由于主动脉和大静脉也在南北方向上，加上水分子也在南北方向上排列整齐，因此以水分为主要成分的血液流动最为顺利畅快，它的惯性有利于通过毛细血管，减少血栓的发生，对血管有自我清洗作用。那么，如果违背了南北睡向会不会损害人的健康呢？这同样与地球磁场作用有着密切的关系。如上所述，地球是一个无比巨大的磁场，其磁力线由北极出来，经地球表面而进入南极。人体的生物电流通道与地球磁场的磁力线方向相互垂直，地球磁场的磁力就成为人体生物电流的一种阻力，要恢复正常运行达到平衡状态，必须消耗大量的热量，提高代谢能力。长此以往，当机体从外界得不到足够的能力补充，能量消耗太大，地球磁场阻力得不到排除时，气血运行就会失常，产生病态；同时，为了达到新的平衡状态，消耗的热量以热的形式围绕在床上，使得睡觉时人体温度升高，心里烦燥，难以入睡。
        <br> 根据这个道理，在睡觉时应该把睡眠方向改为南北方向，而且是头北脚南，取这种“睡向”，人体内的生物大分子就会从方向的排列改成定向排列，人体内的电流方向即气血运行方向达到与地球的磁场磁力线平衡。在磁场力的作用下，气血运行畅通，代谢降低人，能量消耗减少，一觉醒来，就会感到身体轻松，精力充沛。
        <br> 给自己充足的睡眠时间
        <br> 要想提高睡眠质量，入睡时间必须注意； 能取得较好的睡眠质量的入睡时间是晚上9点到11点，中午12点到1点半，凌晨2点到3点半，这时人体精力下降，反应迟缓，思维减慢，情绪低下，利于人体转入慢波睡眠，以进入甜美的梦乡。
        <br> 什么样的睡眠才是最好?睡眠应该是一种无意识的愉快状态。
        <br> 就算睡的时间短，而第二天起床能够很有精神，就表示有好的睡眠‘品质’，但是如果在睡了很久之后仍然觉得很累，就表示睡眠质量很差。
        <br> 睡眠时间因人而异，虽说成年人平均每天睡眠时间为八小时，但其实足够的睡眠不是从时间上的多与少来分别，而是视乎能否达到熟睡的状态，适之宝建议顾客的睡眠时间一般应维持７～８小时，视个体差异而定。如果的确入睡快而睡眠深、一般无梦或少梦者，睡上６小时可完全恢复精力，当然未有不可；而入睡慢而浅，睡眠多、常多梦者，即使睡上１０小时，精神仍难清爽，应通过各种治疗，以获得有效睡眠，单纯延长睡眠时间也对身体无益。如果你每天睡眠超过12小时的话，除非是病了，否则反而会产生"越睡越累"的情况。
        <br> 睡眠要适量
        <br> 1、我们的一个重要观点是：觉不可少睡。在很多书上都说，成年人一般每天睡7－8个小时就差不多了。可是最近美国心理学教授詹姆斯?马斯博士指出：一个人晚上睡眠6－7个小时是不够的。他对睡眠研究的结果表明，只有8个小时睡眠才能够使人体功能达到高峰。所以什么是“适量”，主要是“以精神和体力的恢复”作为标准。
        <br> 人的睡眠分为慢动眼睡眠和快动眼睡眠两个时向。浓度的快动眼睡眠在记忆储存、维持组织、信息整理及新的学习、表现等都发生在快动眼睡眠的最后阶段，而快动眼睡眠通常发生在8小时睡眠期的后部，并可以持续90分钟左右。虽然我们可能并没有觉察到，但是，我们当中大部分人的睡眠其实都是不够的，这不仅降低了生活质量，还可能引发疾病。
        <br> 为了弥补这种普遍的睡眠不足，马斯博士提倡“小睡”。这种小睡是指每天正式睡眠醒来后再小睡20分钟，其效果比晚上早睡要好得多。
        <br> 我们特别强调的是，现在中小学生虽然说“减负”了，但是由于各种各样的考试压力，他们并不轻松，很多人睡眠的时间明显不足。与过去相比，实际上是明松暗紧。这实际上无论对社会还是对家庭都是得不偿失的。我们认为，只有睡好觉，才能学习好。睡好觉并不会妨碍前程：睡眠时间必须保证！
        <br> 另外，对于容易失眠的人来说，应在有睡意的时候才上床，早早上床的结果往往是“欲速则不达”，只会加重心理压力。有人曾经进行过这样的试验，在某些情况下，晚睡早起，减少睡眠时间，而有利于提高睡眠质量。
        <br> 细意安排理想的睡眠环境
        <br> 最适当的睡眠环境，至少应具备安静、遮光、舒适等这些基本条件。噪音的敏感度因人而异，任何声响超过60分贝，自然会刺激你的神经系统，信息还可以传递全身，让你无法安稳入睡。关灯睡觉当然与省电无关，而是因为黑暗的环境能让眼睛快点进入休息的状态，如果你太害怕黑暗，则不妨开一盏小壁灯，尽量调控较微弱的光线，这样便放心入眠。
        <br> 温度与湿度卧室的温度、湿度及空气流通度都是不容忽视的。太热或太冷的室温都会影响睡眠，温度应在摄氏21度至24度左右，依个人的体质而调整。最理想的湿度应是在百分之六十至七十，如果未能合乎标准，可以用冷暖气机或自动除湿机来自动调整室内温度及湿度。睡觉的时候，氧气也是很重要的，因此必需保持空气流通，切勿因为怕冷而关闭所有门窗。
        <br> 注意电磁场的影响力，室内的电磁场对个人健康有着莫大的关系，睡眠前应该尽量将室内的手机或产生电磁场的电器关闭。强大的电磁场会影响我们的生理运作，例如会抑制褪黑激素的分泌，褪黑激素对睡眠有直接促睡作用。研究发现，睡在比一般城市日常生活高五倍的50Hz电磁场环境时，睡眠效率会改变，熟睡期的时间会较短。至于地球磁场影响睡眠品质的说法，则众说纷纭，据说拿破仑相信睡觉时必须头朝北、脚朝南，以配合大地磁场，尽管他每天只睡3小时，也能消除一天的疲劳。适之宝认为，宁可以信其有，床在安放时，应该南北顺向，睡时头北脚南，使机体不受地磁干扰。
        <br> 不少青少年朋友因为经常处在某种噪声中，习惯成自然，适应了这种不良的环境。这对我们的睡眠是不利的，会减少我们浓度睡眠的时间。所以睡觉的环境应该尽量避免噪音干扰。
