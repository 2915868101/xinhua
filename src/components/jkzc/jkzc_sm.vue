        <br> 儿童时期，此激素分泌呈现夜多昼少的规律，晚上1点到凌晨5点之间释放的生长激素差不多是白天的3倍。显然，如果婴幼儿长期晚睡，必将影响生长激素的正常生理分泌，对生长发育颇为不利，尤其是对身高影响较大。
