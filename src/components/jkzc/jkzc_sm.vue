        <br> 1．影响大脑的创造性思维
        <br> 科研人员研究认为，人的大脑要思维清晰、反应灵敏，必须要有充足的睡眠，如果长期睡眠不足，大脑得不到充分的休息，就会影响大脑的创造性思维和处理事物的能力。
