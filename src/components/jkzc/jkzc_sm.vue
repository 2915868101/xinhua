        <br> 药物和睡眠
        <br> 巴比妥类缩短异相睡眠时间，相应增加慢波睡眠时间。许多镇静剂均减少异相睡眠。苯丙胺是一种兴奋剂，其作用也是减少异相睡眠。已知长期服用安眠药的患者，在停药后异相睡眠增多，并发多梦、恶梦，这往往是患者再去服药的原因。酒精的短期作用是缩短异相睡眠时间，但长期服用则使之延长。已知能增加异相睡眠的药剂有利血平是一种治疗高血压病的药，和麦角酰二乙胺（LSD）的一种致幻剂。由于它们都有临床副作用，故限用于睡眠的动物实验研究。
        <br> 睡眠对于大脑健康是极为重要的。未成年人一般需要有8个小时以上的睡眠时间，并且必须保证高质量。如果睡眠睡眠的时间不足或质量不高，那么对大脑就会产生不良的影响，大脑的疲劳就难以恢复，严重的可能影响大脑的功能。青少年如果睡眠不足或睡眠质量差，就应适当增加睡眠的时间，比如夏天午睡片刻，并且要设法改善睡眠状况等。
        <br> 按照一般的观点，睡眠是消除大脑疲劳的主要方式。如果长期睡眠不足或睡眠质量太差，就会严重影响大脑的机能，本来是很聪明的人也会变得糊涂起来。很多青少年学生患上了神经衰弱等疾病，很多时候就是因为严重睡眠不足引发的。
        <br> 总之，一个人的一中，有三分之一多的时间是在睡眠中度过的。正当的良好睡眠，可调节生理机能，维持神经系统的平衡，是生命中重要的一环。睡眠不良、不足，第二天就会头错脑胀、全身无力。睡眠与健康工作和学习的关系甚为密切。
