      <p class="f-tit">
        <span>对生理的影响</span>
      </p>
      <p class="f-cont">
        有生理学实验表明，缺乏睡眠的人，不仅免疫能力大幅度降低，每天的衰老进程是正常人的4-5倍，下文就睡眠对生理组织的影响，作出了系统的探讨和研究：
        <br> 1.睡眠与细胞寿命
        <br> 缺乏睡眠对细胞氧化更新的后果，其主要导致组织的损坏，这一过程是通过酶的作用来修复的，对于分子间的不断修复和细胞的总体最高寿命是分不开的，就衰老而言，最重要的检出DNA的损伤并修复他们，而细胞的完整性又是其分裂增殖寿命的基础，而这种修复机制是在生命体进行高深度睡眠下完成的。
        <br> 有科研实验表明，高强度的睡眠时间是平均机体花费在修复DNA损伤的力度最大，在细胞分子学上来说，DNA损伤与DNA修复间的这种平衡是细胞衰老的最重要指标。
        <br> 2.睡眠与机体免疫
        <br> 无论是缺乏睡眠，还是缺乏完整的睡眠，对于人体的免疫系统是崩溃性的打击，人体的胸腺会急剧性萎缩。这是人体最重要的免疫器官，在通常情况下，只有在中老年时期，胸腺会缩小成一个樱桃大小，而T细胞的增值会大幅度下降，不仅白细胞和淋巴细胞大量减少，并使巨噬细胞有了抑制作用。
        <br> 在人体缺乏免疫系统的保护下，各种致癌疾病对人体的威胁可以说是致命的。即使是在短期，人体会感到恶心，昏头昏脑，精神萎缩，四肢乏力，这不仅是免疫力直接下降的生理趋势，还是疾病的前兆。
        <br> 3.睡眠和神经系统
        <br> 神经系统是机体主要的机能调节系统，它直接地调节着机体内各器官，系统的机能，而睡眠与中枢神经的生理作用这一关系是紧密分不开的， 作为一种调节机制，睡眠是通过下丘脑是用于调节内脏的活动及激素内分泌.，而神经内分泌不仅取决于中枢神经的指令，更是生命体本有的“生物钟”的活动周期。
        <br> 从生理学来说，生物钟本就是DNA起遗传作用的记忆编码，如下丘脑组织，在人体深度睡眠下，平均夜晚10点到左右会分泌还原性谷胱甘肽，超氧化物歧化酶(SOD）等等，这些都是不可合成的人体必需激素，主要用来修复细胞的损伤，促进细胞的增殖与分化，确保各组织、各器官的正常生长、发育。
        <br> 4.夜晚睡眠的重大意义
        <br> 实验已经表明，即使是短期性的缺乏睡眠以及缺乏足够的睡眠时间，人体会急剧化的衰老，免疫力大幅度低下。
        <br> 夜晚的深度睡眠，通过下丘脑的神经中枢作用，实则是调节各种组织细胞的代谢活动来影响人体的生理活动，主要用来修复细胞的损伤以及还原组织功能的完整性。
        <br> 生物钟是受大脑的下 丘脑"视交叉上核"（简称SCN）控制的，通过内分泌激素的分泌周期向中枢神经系统发放“时间信号”，从而影响机体时间生物效应，这说明机体的修复机制必须是在夜晚进行的。
