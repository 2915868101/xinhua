        <br> 好的卧具可助你入睡，睡好，并防止睡眠时损伤颈、背。请从选择好的床垫开始。羽绒制品使你更舒适并比传统卧具轻巧。
        <br> ⒌请别吸烟
        <br> 研究表明，重度吸烟者难眠、易醒，少有REM及深度NREM睡眠。因为残存的尼古丁可在吸入最后一口后2～3小时内失去作用，所以吸烟者常在夜晚渴望吸烟。吸烟者改变其吸烟习惯，则会戏剧性地改善其睡眠。一次研究表明，一天两包的吸烟者若戒烟，则其辗转难眠的时间会减少一半。
        <br> ⒍只喝软饮料
        <br> 酒精是最古老、最广泛的助睡剂。即便许多人习惯于晚上喝上一杯，但这也许意味着整晚的麻烦。晚餐饮酒过量导致难以入眠，睡眠时间内饮酒则导致难以睡好。随酒精而来的影响消失后，REM睡眠（受酒精抑制）占用其他睡眠阶段，使身体得不到深层次的休息，睡眠会被分割得支离破碎，而清晨则频繁醒来。
        <br> ⒎追求质量，而非数量
        <br> 六小时的优质睡眠比八小时的低质睡眠能使人体得到更好的休息。把睡眠时间严格控制在所需范围内，会加深睡眠。请别认为你必须躺八小时，若五小时可使你充足电，那么暗自庆幸吧。你不是失眠患者，而是天生的短时睡眠者。
