        <span>睡眠是修补心灵的活动</span>
      </p>
      <p class="f-cont">
        人的睡眠包括两部分：速眼动与非速眼动睡眠。人类睡眠分为两个非常重要的部分，当睡眠时眼皮下方的两颗眼球呈现出规则性的左右摆动，这种特殊睡眠称为速眼动睡眠。整夜有规律地交错进行着，睡眠医学上叫做睡眠循环。一个晚上会有4至5次的睡眠循环，非速眼动睡眠在前半夜维持较多，后半夜几乎没有；但速眼动睡眠到后半夜则是愈来愈多。
        <br> 非速眼动睡眠在前半夜较多，主要是常人经过一天的用脑后，脑力消枆很大，对于脑细胞与脑组织都是压力与伤害，生理时钟自然会诱使我们睡觉，让脑部温度降下，进入修护阶段，这对生命的维系相当重要。后半夜睡眠速眼动睡眠会较多，则是与学习工作有关，当一天中经历了许多不管是好是坏的景物，都要纳入记忆中，而速眼动睡眠就包含了学习新事物、整合日间情绪等作用。
