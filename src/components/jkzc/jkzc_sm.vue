        睡眠是一个系统工程
        <br> 其实，睡觉并不是倒在枕头上，盖上被子合眼一宿，再睁开眼睛醒来这么简单，它是一个复杂的渐变过程。为什么我们有时醒来后，身体会神奇般地恢复力量，而有时却感觉比睡觉之前还累呢?这便是是睡眠的深度和状态所不同而导致的。
        <br> 芝加哥大学的研究人员，在对几千名志愿者进行过睡眠时的脑电波记录后，揭示了人体的睡眠周期：在睡眠中，人体首先进入慢波睡眠期，后是快眼动睡眠期，之后再重复开始，一夜大约有4-6个睡眠周期。
        <br> 美国研究发现，凌晨两点后才入睡容易打乱生理时钟，对身体脂质新陈代谢带来严重影响，容易增加心脏病风险。
        <br> 我们到底需要睡多久
        <br> 睡眠不足所带来的害处，相信我们都耳熟能详了：精力不济、反应迟钝、记忆力衰退、免疫力降低，甚至是机体的过早衰老。目前最普遍的说法是，一个成年人应该拥有的睡眠时间是7至8个小时，但美国加州大学的一项实验却得出了个耸人听闻的结论：“每天睡8小时会让你死得更快。”
        <br> 研究发现，每天仅睡6、7个小时的人，比每天睡超过8小时，或少于4小时的人死亡率要低很多。其中，每天睡7小时的人死亡率最低，而即使是只睡5小时的人，这个系数也要低于睡够8小时的人。
        <br> 睡眠节律的恶性循环
        <br> 那些夜里常常醒来，或者在还未得到充分休息以前就醒来的人，他们的睡眠节律是很混乱的，脑电波图在各阶段都显示出快速、急剧升降和受到抑制的波型，这在正常人睡眠中是见不到的。
        <br> 因此，只有充分进行好了4-5期的深度睡眠，人体的生理机能才能得到充分的修复，免疫系统能够得到加强，而能量也能得到充分补充。如果一味地赖在床上，却没有得到高质量的睡眠。这对于人体反而是有害无益的，它甚至会缩短你的生命。
        <br> 这种恶性循环周而复始下去，结果就是你的睡眠系统被削弱。生理休息期被打乱，身体得不到足够的能量，又让你的免疫力降低了。
        <br> 睡得过多和吃得过饱
        <br> 那些睡得更多的人，并不是因为他们需要长时间睡眠，而是因为他们没有好好地关照自己的睡眠系统，从而导致这个系统被削弱，不能高效率地工作。
