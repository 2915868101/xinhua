        睡眠从来就是人们感兴趣的研究课题，因为人的生命约有1/3是在睡眠中度过的，并且睡眠可以恢复精神和解除疲劳。自20世纪初，借助脑电图的分析，发现睡眠时脑电活动呈现特殊慢波。1952年又有人发现睡眠过程中经常发生短时间的、快速的眼球运动，并伴有快速低幅的脑电波。这一重要发现导致睡眠研究的蓬勃发展。已经证明。睡眠是大脑的主动活动过程，而不是被动的觉醒状态的取消；脑内许多神经结构和递质参与睡眠的发生和发展（从而开展了梦的生理学研究以及临床睡眠紊乱症的鉴别诊断和治疗等）。但至今对睡眠的起源、发生和发展的机制，时相转换的生理基础，以及睡眠如何消除疲劳等重要问题仍然不甚了解。
        <br> 正常睡眼的时相 睡眠由两个交替出现的不同时相所组成，一个是慢波相，又称非快速眼动睡眠，另一个则是异相睡眠，又称快速眼动睡眠，此时相中出现眼球快速运动，并经常做梦。
        <br> 慢波睡眠
        <br> 根据人脑电波的特征，通常将此时相区分为4个不同的期，即相应于睡眠由浅入深的过程。第1期呈现低电压脑波，频率快慢混合，而以4～7周/秒的频率为主，它常出现在睡眠伊始和夜间短暂苏醒之后。第2期也是较低电压脑波，中间插入短串的12～14周/秒的睡眠梭形波和K复合波，它是慢波睡眠的主要成分，代表浅睡过程。第3期的脑电图常有短暂的高电压波，超过50微伏，频率为1～2周/秒，叫做δ波。第4期，δ波占优势，其出现时间占总时间的50％似上，代表深睡状态。因此，3、4两期仅有量的差别，而无质的差异。一般认为慢波睡眠第4期具有消除疲劳的功能，因为人在长时间体力劳动或不睡后，在恢复睡眠中此期延续很久。随着睡眠由浅入深，意识逐步丧失，血压略降，心率、呼吸减慢，瞳孔缩小，体温和代谢率均下降，尿量减少，胃液增多，唾液分泌减少，发汗功能增强等。
        <br> 异相睡眠
        <br> 它是在睡眠过程中周期出现的一种激动状态。脑电图呈现快频低压电波，类似清醒时脑波。自主神经系统活动增强，如心率、呼吸加速，血压升高，脑血流及耗氧量均增加，在男性则有阴茎勃起。此外，睡者时时翻身，面和指（趾）端肌肉不时抽动。在实验动物还记录到单个神经细胞的放电活动非但高于慢波相，有时还超过清醒状态下的活动水平。人的异相睡眠，和动物的一样，表现出3个特征：①低电压，快频脑波；②颈部肌肉张力松弛以及脊髓反射被抑制，此时运动系统受到很强抑制；③频繁出现快速的眼球运动，同时在一些和视觉有关的脑结构，包括大脑皮层视区，出现高大锐波，统称脑桥-膝状体-枕区皮层波（PGO）。由于快速眼动只存在于异相睡眠中，故后者常被叫做快速眼动睡眠。
        <br> 睡眠时相的转换 正常成年人入睡后，首先进入慢波相，通常依次为1～2～3～4～3～2等期，历时70～120分钟不等，即转入异相睡眠，约5～15分钟，这样便结束第1个时相转换，接着又开始慢波相，并转入下一个异相睡眠，如此周而复始地进行下去。整个睡眠过程，一般有4～6次转换，慢波相时程逐次缩短，并以第2期为主，而异相时程则逐步延长。以睡眠全时为100％，则慢波睡眠约占80％，而异相睡眠占20％。将睡眠不同时相和觉醒态按出现先后的时间序列排列，可绘制成睡眠图，它能直观地反映睡眠各时相的动态变化。
        <br> 睡眠深度
        <br> 一般是以身体活动减少和感觉灵敏度降低作为衡量的指标。此外，一些生理指标，特别是唤醒阈，也指示慢波相的第3、4期是深睡时期。至于异相睡眠的深度则很难判定，因为它即表现肌张力松弛，又常出现全身翻转和面、指肌抽动；在感觉方面，外界无关的刺激较难唤醒睡者，可是当刺激具有特殊含义或者和他做梦的内容有关时，则极易唤醒。这些矛盾提示，在异相睡眠中脑内发生一种主动过程能切断它和外界无关刺激的联系。如果依自主神经系统活动强弱来判别，则异相睡眠更接近觉醒状态，如在此时相唤醒睡者，他会说自己正在熟睡；反之，在慢波相时唤醒他，则说睡得不熟。推测这种主观的睡眠意识可能与他的梦境有关联。综上所述，对睡眠深度的精确测定是困难的，目前的趋向是将异相和慢波相看作两个独立的状态。
        <br> 有些自主神经活动随睡眠过程的发展而变化，似和两个时相关系不大。例如，体温从睡眠开始便逐渐下降，5～6小时达最低点，然后又逐渐回升。有人提出，睡眠时仍能学习口述材料，可是脑电图的分析证明，睡者实际上是处在朦胧状态。梦呓多发生在慢波睡眠的第2期，而梦游则无例外地发生在慢波第4期中，并且两者一般都和梦的内容无关。
        <br> 个体发生
        <br> 新生儿平均每天睡16小时，婴儿睡眠时间逐渐缩短，至2岁时约睡9～12小时。成年人的睡眠时间因人而异，通常为6～9小时不等，一般认为7小时半是合适的。可是老年人的睡眠经常少到6小时。根据脑电图的分析，新生儿的异相睡眠约占睡眠总时间的50％，并且入睡后很快就进入异相时期，成年人约占20％，而老人则不到20％。在成年人凡异相睡眠时间低于15％或高于25％的则被认为不正常。同样，慢波相第4期也随年龄增长而逐渐减少。至于睡眠与觉醒的周期更替，新生儿一天中约5～6次，婴儿逐渐减少，学龄儿童每天约1～2次的睡眠。有些老年人又恢复一日睡几次的习惯。以上随年龄增长所产生的睡眠变化可能和个体发育，特别是脑的觉醒系统的发育有关，此外也和家庭、学校环境以及社会活动的需要有关。在哺乳动物，随着脑发育进程的不同，睡眠的成熟过程也不相同，例如，初生小猫出生第1天几乎都是异相睡眠，从第2天起出现慢波睡眠，一月后，接近成年猫的睡眠类型；大鼠则在出生后两周内，几乎全部是异相睡眠，一月后，异相睡眠缩短到10％；初生豚鼠异相睡眠不到10％，随后很快发展到低于5％。
        <br> 种系发生
        <br> 按照上述睡眠的3大特征：周期性、自发性和可逆性，并以动物的安静不动和感觉敏感性降低作为衡量指标，则从高等脊椎动物才开始出现睡眠行为和脑电变化。在鱼、两栖和爬行动物，可以观察到觉醒和睡眠的周期转换，但无异相睡眠。至温血动物鸟类，才有明显的异相睡眠，约占睡眠时间的3～5％。哺乳类动物从鼠到象，都有明确的慢波相和异相睡眠，不过它们各自所占比重随种别而异。若从动物生存竞争角度来分析，大体可归为两类：一类是被猎食动物，如啮齿类、食草及反刍动物摄食后需要长时间咀嚼，因而睡眠时间缩短，异相睡眠也短，一般不超出5％，如母牛为1.6％，食蚁兽居然没有异相睡眠；另一类是猎食动物，如食肉类，它们的进食快，有较多时间的睡眠，异相睡眠也较长，可达20％或更长，如猫、狗等都如此。
        <br> 剥夺睡眠
        <br> 实验性地剥夺人或动物的睡眠是一种研究睡眠的生理意义及其必要性的可行方法。剥夺全部睡眠 24～48小时后，脑电频率变慢，近似慢波相第1期，但外观行为正常。继续剥夺，则警觉性降低，严重的会发生幻觉、谵妄或梦呓。3～4天后，任其入睡，在第一夜中慢波第4期明显增多，而异相睡眠相应减少，在以后数晚，异相睡眠才代偿地增多。一例11天不睡的青年，入睡后，慢波第4期和异相睡眠都明显增加。相反，被剥夺睡眠的猫在第1夜增多的是异相睡眠，而不是慢波第4期睡眠。
        <br> 剥夺部分睡眠 使受试者每天仅睡3～4小时，几天后，异相睡眠或分比平时睡眠头4小时内的异相成分加多。停止睡眠实验后，受试者连续数夜多次出现异相睡眠。在长期每天只睡4小时的受试者慢波第4期增多，第3期相??如将受试者睡眠缩短到3小时以下，则会影响第4期睡眠的发展，从而严重影响受试者的工作能力。
        <br> 选择剥夺睡眠某时相 如果选择剥夺慢波第4期睡眠，则在恢复过程会代偿地只增加第4期睡眠；如选择剥夺异相睡眠，也产生同样的代偿情况，许多心理学实验结果表明，长期剥夺异相睡眠不产生明显的心理紊乱；而长期剥夺全部睡眠的人，不能长时间工作，否则差错加多。这可能和大脑皮层不能持久维持警觉状态有关。此外，少数人还会发生短暂幻觉和荒诞行为。总之，这些实验结果似不支持长期不眠或严重失眠会导致精神变态的假说，可是从另一方面却表明，长期缺乏睡眠极易引起疲劳，注意力不集中以及视、触觉的错乱等。
        <br> 病态睡眠
        <br> 失眠症 根据失眠发生的时间先后，可区分3种失眠症。①发生在睡眠初期，表现为很难入睡，也是最常见的失眠症。②表现为全夜时醒时睡。③发生在睡眠终期，患者过早苏醒，不能再入睡。这些患者的异相睡眠都少，并易诱发脑电的唤醒反应。从脑电图分析波看，他们的睡眠时间总是比主诉的为多，失眠的后果并不严重，长期失眠者有时精神萎靡，可用药物洽疗纠正。
        <br> 多眠症 表现为白昼多眠或瞌睡过多，或者夜间睡眠过久。患者睡眠期间心率并不减慢，说明患者在睡眠期间休息不充分。原发性多眠症多属遗传病，这和下丘脑功能障碍有关。此外，还有一种伴有食欲亢进，肥胖和呼吸不足等症的多眠症。
        <br> 发作性睡眠症 发作时患者突然入睡，不能自控，但只持续数秒至数分钟，还经常伴发由肌张力丧失产生的猝倒。发作时脑电波和异相睡眠的相似，多属先天性的。中枢兴奋药可减轻其症状。
        <br> 梦游 此症发生在慢波睡眠的第3、4期，也是回忆能力最低的时期。与患者做梦无关。梦游时大脑警觉性和反应性均降低，运动也欠协调。
        <br> 遗尿症 多半发生在睡眠的前1／3时期，脑电波呈慢波第4期型式。遗尿开始即转入2期或1期。唤醒后患者不诉梦境，若不唤醒，则在数小时内不再出现3或4期脑波。遗尿症可用药物或其他方法治疗。
        <br> 睡眼机制 И．П．巴甫洛夫观察到狗在不强化的条件反射实验中会自然入睡，于是提出内抑制过程扩散到大脑皮层和皮层下结构便产生睡眠的学说。但以后的实验研究未能提供更明确的支持论据。1949年意大利的G．莫鲁齐和美国的H．W．马古恩发现中脑的上行网状激舌系统是产生大脑觉醒状态的重要结构，随之有人设想，睡眠仅是觉醒活动暂时中断的结果，而疲劳则是引起中断的重要原因，这种认为睡眠为被动过程的看法，和切断中脑或切断感觉神经传入纤维所造成的睡眠状态的实验结果相吻合。长期积累的神经生理学实验资料表明，局部电刺激动物下丘脑或脑干的一些结构均可引起睡眠，以后人们发现异相睡眠中机体处于激动状态，说明睡眠是一个主动活动过程，并提出存在“睡眠中枢”的观点。从60年代起开展了大量实验研究工作。莫鲁齐等提出脑干尾段存在睡眠中枢的假说。他们认为延髓和脑桥内存在上行抑制系统，这一系统的活动可以导致睡眠。此系统一方面接受来自躯体和内脏的感觉传入冲动，另一方面又受到前脑梨状区皮层、扣带回和视前区等结构的下行控制。与此同时，M．儒韦提出关于觉醒-睡眠周期的单胺学说。他认为从蓝斑前部发出的上行去甲肾上腺素能系统，维持大脑皮层觉醒态电活动，加上乙酰胆碱能系统的活动，才能完成注意、学习、记忆等高级功能；而黑质-纹状体环路的多巴胺能系统，则维持觉醒的行为表现，至于睡眠过程，则由中缝核群前段发出的上行5-羟色胺能系统维持慢波睡眠，而其中段核群一旦触发蓝斑区域细胞活动后，后者的上行冲动激活大脑皮层电活动，使之出现快频低幅波，同时，其下行冲动则抑制脊髓运动系统，从而形成异相睡眠。但作者未说明睡眠怎样开始。应当指出，这两种假说并无根本分歧。因为他们争论的仅是和睡眠有关的脑结构的部位。当今许多工作正是沿这两种设想向纵深发展。从70年代起，莫尼埃和J．R．帕彭海默分别从入睡的兔
