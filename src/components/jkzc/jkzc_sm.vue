        <br> ⒈守时
        <br> 为保持你生物钟的同步性，不论睡得多长或是多短，请你每日于同一时间起床。
        <br> 若你周五和周六晚至次日凌晨才睡觉，你也许会患上“周日失眠症”。星期日早早上床，两眼放光，极力入睡却无能为力。你越是努力，越感疲乏。
        <br> 当旅行或工作打破日常生活的规律，你应尽量保持定时进餐和睡眠的习惯，并尽早恢复日常作息时间。
        <br> ⒉定时运动
        <br> 运动可通过缓解白天所累积的紧张并使得身心放松而增进睡眠。
        <br> 常参加体育锻炼者比不常锻炼者睡得更好更深，但你不必刻意追求过度疲劳。每周至少三天，每次20～30分钟的散步、工作、游泳或骑车应是你的目标———有益于心血管的最低限度。
        <br> 但是别等到太晚才运动。理想的运动时间是下午晚些时候或傍晚早些时候，此时体育锻炼可帮助你从白天的压力调整到晚上的愉快。 ⒊减少兴奋剂的摄入
        <br> 若你爱喝咖啡，请在上床前8小时(6点）以前喝一天中最后一杯咖啡。其兴奋作用将在2～4小时后达到顶峰，并还将持续几小时。晚上摄入咖啡因使你更难入眠或不能深睡并会增加醒来的次数。
