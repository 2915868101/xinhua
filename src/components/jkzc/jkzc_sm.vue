        <br> ⒊睡觉摆个什么pose？
        <br> 《枕头记》记录说，右侧位睡眠有助于安睡。睡觉的时候，尽量避免将双手放在胸前，压迫心肺，阻碍呼吸。仰卧不是最理想的姿势，因为仰卧时舌根部往后坠缩，影响呼吸，容易发出鼾声。伏卧会压迫胸腹，容易导致呼吸困难，都是不正确的睡姿。向左而卧也不是好方法，因为压着左心室活动对血型循环。一般人以侧卧为佳。仰卧入睡时四肢肌肉得不到放松。睡熟之后手会不自觉地搭压在胸上，容易引起噩梦；俯卧时胸腹部受压，口鼻易被枕头捂住，还容易"落枕"。侧卧时四肢放松，活动范围不大，不易打鼾。如果有某种疾患，最好听取医生意见。对于患有某种疾病或有特殊情况的人而言，讲究一下睡眠姿势就很有必要。因为很多疾病是由于睡眠姿势不当而诱发或加重的。所以，自行或被动采取保护性睡姿，对预防疾病的发生或减轻疾病的症状都是有益的。
        <br> ⒋给自己充足的睡眠时间
        <br> 要想提高睡眠质量，入睡时间必须注意； 能取得较好的睡眠质量的入睡时间是晚上9点到11点，中午12点到1点半，凌晨2点到3点半，这时人体精力下降，反应迟缓，思维减慢，情绪低下，利于人体转入慢波睡眠，以进入甜美的梦乡。
        <br> 什么样的睡眠才是最好
        <br> 睡眠应该是一种无意识的愉快状态。
        <br> 睡眠时间因人而异，虽说成年人平均每天睡眠时间为八小时，但其实足够的睡眠不是从时间上的多与少来分别，而是视乎能否达到熟睡的状态，专家建议的睡眠时间一般应维持7～8小时，视个体差异而定。如果的确入睡快而睡眠深、一般无梦或少梦者，睡上6小时可完全恢复精力，当然未有不可；而入睡慢而浅，睡眠多、常多梦者，即使睡上10小时，精神仍难清爽，应通过各种治疗，以获得有效睡眠，单纯延长睡眠时间也对身体无益。
        <br> 睡眠要适量
        <br> 我们的一个重要观点是：觉不可少睡。在很多书上都说，成年人一般每天睡7－8个小时就差不多了。可是美国心理学教授詹姆斯?马斯博士指出：一个人晚上睡眠6－7个小时是不够的。他对睡眠研究的结果表明，只有8个小时睡眠才能够使人体功能达到高峰。所以什么是“适量”，主要是“以精神和体力的恢复”作为标准。
        <br> 人的睡眠分为慢动眼睡眠和快动眼睡眠两个时向。浓度的快动眼睡眠在记忆储存、维持组织、信息整理及新的学习、表现等都发生在快动眼睡眠的最后阶段，而快动眼睡眠通常发生在8小时睡眠期的后部，并可以持续90分钟左右。虽然我们可能并没有觉察到，但是，我们当中大部分人的睡眠其实都是不够的，这不仅降低了生活质量，还可能引发疾病
        <br> 为了弥补这种普遍的睡眠不足，马斯博士提倡“小睡”。这种小睡是指每天正式睡眠醒来后再小睡20分钟，其效果比晚上早睡要好得多 我们特别强调的是，中小学生虽然说“减负”了，这实际上无论对社会还是对家庭都是得不偿失的。我们认为，只有睡好觉，才能学习好。睡好觉并不会妨碍前程：睡眠时间必须保证！
        <br> 另外，对于容易失眠的人来说，应在有睡意的时候才上床，早早上床的结果往往是“欲速则不达”，只会加重心理压力。有人曾经进行过这样的试验，在某些情况下，晚睡早起，减少睡眠时间，而有利于提高睡眠质量。
        <br> 细意安排理想的睡眠环境
        <br> 最适当的睡眠环境，至少应具备安静、遮光、舒适等这些基本条件。噪音的敏感度因人而异，任何声响超过60分贝，自然会刺激你的神经系统，信息还可以传递全身，让你无法安稳入睡。关灯睡觉当然与省电无关，而是因为黑暗的环境能让眼睛快点进入休息的状态，如果你太害怕黑暗，则不妨开一盏小壁灯，尽量调控较微弱的光线，这样便放心入眠。
