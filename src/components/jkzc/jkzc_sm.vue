      <p class="f-tit">
        <span>睡眠的坏习惯</span>
      </p>
      <p class="f-cont">
        1、枕头过高
        <br> 从生理角度上讲，枕头以8~12厘米为宜。太低，容易造成 “落枕”，或因流入头脑的血液过多，造成次日头脑发涨、眼皮浮肿；过高，会影响呼吸道畅通，易打呼噜，而且长期高枕，易导致颈部不适或驼背。
        <br> 2、枕着手睡
        <br> 睡时两手枕于头下，除影响血液循环、引起上肢麻木酸痛外，还易使腹内压力升高，久而久之还会产生“返流性食道炎”。所以，睡时不宜以两手为枕。
        <br> 3、被子蒙头
        <br> 以被蒙面易引起呼吸困难；同时，吸入自己呼出的二氧化碳，对身体健康极为不利。婴幼儿更不宜如此，否则有窒息的危险。
        <br> 4、剧烈运动
        <br> 睡前剧烈活动，会使大脑控制肌肉活动的神经细胞呈现极强烈的兴奋状态，这种兴奋在短时间里不会平静下来，人便不能很快入睡。所以，睡前应当尽量保持身体平静，但也不妨作些轻微活动，如散步等。
        <br> 5、对着风睡
        <br> 人体睡眠时对环境变化的适应能力降低，易受凉生病。古人认为，风为百病之长，善行而数变；善调摄者，虽盛暑不当风及生卧露下。所以睡觉的地方应避开风口，床离窗、门有一定距离为宜。
        <br> 6、坐着睡
        <br> 有些人吃饱饭往沙发一坐，打开电视沏壶茶，够舒服的。可能工作太累了，看着电视就睡着了，这就使第二大隐患出现了！因为坐着睡会使心率减慢，血管扩张，流到各脏器的血液也就少了。再加上胃部消化需要血液供应，从而加重了脑缺氧，导致头晕、耳鸣的出现。有人说中午就歇一会儿，谁还带个被子去单位呀，找个地方一呆就得了。
        <br> 7、睡前饱餐
        <br> 睡前吃得过饱，胃肠要加紧消化，装满食物的胃刺激大脑。大脑有兴奋点，人便不会安然入睡，正如中医所说“胃不和，则卧不安”。
        <br> 8、睡前饮茶
        <br> 茶叶中含有咖啡碱等物质，这些物质会刺激中枢神经，使人兴奋，若睡前喝茶，特别是浓茶，中枢神经会更加兴奋，使人不易入睡。
